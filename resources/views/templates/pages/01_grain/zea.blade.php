                <h3 class="my_class_h3">Гниене на стъблата и кочаните – <span class="latin_name">Fusarium spp.</span></h3>
                <h4 class="my_class_h4">Описание на болестта</h4>
                <p>
                    Преобладаващи по царевицата са два вида от род <span class="latin_name">Fusarium: – F. moniliforme и F. graminearum.</span>
                    Вторият вид се среща по-често в потоплите райони. Те се запазват основно в растителните остатъци, но могат
                    да се пренасят и със семената. Спорите, пренасяни по въздушен път, също са източник на инокулум. Симптомите
                    се проявяват по време на узряване. Патогенът напада стъблото откъм почвата, през корените или долните
                    междувъзлия на стъблото. При по-висока степен на нападение основата на стъблата изгнива напълно и стъблата
                    загиват. Частично повредените метлици могат да увиснат, след като са били нападнати. Гниенето на метлиците
                    с причинител <span class="latin_name">F. graminearum</span> може да се развие от върха към основата им.
                </p>
                <p>
                    Царевичните растения са предразположени към заразяване при високи нива на азот и ниски нива на калий, както
                    и при сухи условия, редуващи се периодично с влажно време след фенофаза “изметляване”.
                </p>
                <p>
                    Друг вид от р. <span class="latin_name">Fusarium – F. culmorum</span>, също е представител на комплекса
                    причинители на гниенето на основите на стъблата, но може да причини и гниене на кочаните. След заразяване
                    кочанът и обвивните листа придобиват розов или червеникав цвят, дължащ се на мицела и спороношението на гъбата.
                </p>
                <h4 class="my_class_h4">Стратегия за борба</h4>
                <p>
                    Тъй като при полски условия борбата по химичен път е невъзможна, превантивните мерки се изразяват в
                    културно-стопански практики, които създават условия, неблагоприятни за болестта. Препоръчва се използването
                    на по-малко чувствителни сортове. ДРЗП е засяване в подходящите срокове и при оптималната гъстота на посева
                    с 7–12 растения м<sup>2</sup> за осигуряване на достатъчна здравина на стъблото, което предотвратява нападението от
                    тези гъби. Ако гъбата вече е заразила растенията, най-доброто решение е възможно най-скоро прибиране на
                    реколтата. Като предпазна мярка може да бъде приложено и третиране на семената.
                </p>

