        <div class="aa-title" id="ustilago-maidis">
            <span></span>
        </div>
        <div class="row">
            <div class="pest-description-container col-md-10 text-justifysss text-indent">
                <h3 class="my_class_h3">Обикновена главня – <span class="latin_name">Ustilago maidis</span></h3>
                <h4 class="my_class_h4">Описание</h4>
                <p>
                    <span class="latin_name">Ustilago maydis</span> напада основно онези части на растенията, в които протича
                    интензивно клетъчно делене. Симптомите са сиви тумори по стъблата, кочаните, листата или съцветията. По-късно
                    туморите се разпукват и се освобождават кафеникаво-черни спори. Същите се запазват в почвата за 4 и повече
                    години. Растенията се заразяват от спори, пренесени по въздушен път или съхранени в почвата. Болестта оказва
                    влияние върху количеството и качеството на зърната. Обикновената главня се среща в сухи и топли години,
                    особено когато е налице екстремно засушаване. Обикновено тя е спорадична и няма голямо значение.
                </p>

                <h4 class="my_class_h4">Стратегия за борба</h4>
                <p>
                    Химична борба не се провежда. Сеитбообръщението (напр. 4-годишно) има известен ефект за намаляване на
                    инокулума в почвата. Степента на устойчивост при различните сортове варира. ДРЗП е използването на устойчиви
                    сортове. Нападение от <span class="latin_name">U. maydis</span> може да бъде предотвратено чрез осигуряване
                    на добра структура на почвата и дъждуване на посева при засушаване.
                </p>

