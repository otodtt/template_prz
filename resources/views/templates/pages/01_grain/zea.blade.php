                    и ако е така, да се избере най-подходящият момент за прилагане на ПРЗ.
                </p>
                <p >
                    • Използваните ПРЗ трябва да бъдат най-подходящите за съответния вредител в съответния срок на прилагане.
                    ДРЗП е да се използват подходящи техники на прилагане и да се намалят течовете и нежеланото разпиляване.
                    ПРЗ трябва, доколкото е възможно, да е специализиран за съответния вредител и да не води до развитието на
                    устойчивост (от страна на вредителя).
                </p>
