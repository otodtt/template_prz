        <div class="aa-title" id="weed">
            <span></span>
        </div>
        <div class="row">
            <div class="pest-description-container col-md-10 text-justifysss text-indent">
                <h3 class="my_class_h3">Плевели</h3>
                <p>
                    Плевелите са сериозни конкуренти на царевицата, особено в ранните фази на нейното развитие. При забавено
                    поникване и растеж, вследствие на ниски температури, конкурентоспособността на културата е намалена значително
                    по отношение на плевелите. Намаляването на загубите, предизвикани от плевелите, може да се постигне чрез
                    своевременното им отстраняване.
                </p>
                <p>
                    Намножаването и масовото разпространение на многогодишни коренищни и кореновоиздънкови плевели в обработваемите
                    площи създава сериозни проблеми при отглеждането на царевицата. Основни причини за разпространението на
                    тези видове плевели са: пропуски в агротехниката (срокове, начини и дълбочина на обработките, използването
                    на фрези и дискови оръдия, липса на научнообосновани сеитбообръщения), неправилно и ненавременно използване
                    на хербициди и др. Монокултурното отглеждане на царевицата също влияе благотворно върху намножаването на
                    многогодишните видове плевели балур, паламида, повитица и др. Настъпилите компенсационни процеси в
                    плевелните асоциации и намножаването на проблемни плевели се дължат и на прилагането на хербициди на
                    една и съща база. Широкото използване на триазинови хербициди в недалечното минало е причина за формирането
                    на резистентност при редица икономически важни видове плевели.
                </p>
                <h4 class="my_class_h4">Стратегия за борба</h4>
                <p >
                    Всички тези проблеми могат да се разрешат чрез интегрирано прилагане на културно-стопанските методи и ротиране
                    на хербициди от различни групи и с различен механизъм на действие. Основни видове плевели, които заплевеляват
                    царевичните посеви, са: – от групата на късните пролетни плевели – кокоше просо
                    (<span class="latin_name">Echinochloa crus-galli</span>), видовете кощрява (<span class="latin_name">Setaria sp.</span>),
                    кръвно просо (<span class="latin_name">Panicum sanguinale</span>), бяла лобода
                    (<span class="latin_name">Chenopodium album</span>), видовете щир (<span class="latin_name">Amaranthus sp.</span>),
                    обикновено грънче (<span class="latin_name">Hibiscus trionum</span>), лападоволистно пипериче
                    (<span class="latin_name">Poligonum lapathifolium</span>), черно куче грозде (<span class="latin_name">Solanum nigrum</span>),
                    просфорник (<span class="latin_name">Abutilon theophrasti</span>), европейска знойка
                    (<span class="latin_name">Heliotropium europaeum</span>), татул (<span class="latin_name">Datura stramonium</span>),
                    свиница (<span class="latin_name">Xanthium strumarium</span>), пача трева (<span class="latin_name">Poligonum aviculare</span>) и
                    др.; – някои видове от ранните пролетни плевели –
                    синап (<span class="latin_name">Sinapis arvensis</span>), фасулче (<span class="latin_name">Falopa convolvulus</span>) и др.
                    От многогодишните коренищни и кореново-издънкови плевели особено големи проблеми при царевицата създават балур
                    (<span class="latin_name">Sorghum halepensis</span>), полска паламида
                    (<span class="latin_name">Cirsium arvense</span>), полска поветица (<span class="latin_name">Convolvulus arvensis</span>),
                    млечок (<span class="latin_name">Sonchus arvensis</span>) и др.
                </p>
                <p>
                    Ефективна борба срещу посочените видове плевели може да се постигне чрез прилагане на предпазни и агротехнически
                    мероприятия, умело съчетани с правилен избор на хербициди и навременното третиране с тях. При силно
                    заплевеляване с многогдишни плевели добри предшественици на царевицата са културите със слята повърхност
                    (пшеница, ечемик), които освобождават по-рано площите и създават условия за подходящи обработки на почвата.
                </p>
                <p>
                    Механичната борба срещу многогодишните видове плевели трябва да бъде насочена към изсушаване, изтощаване
                    и унищожаване на подземните им органи. Провеждането на дълбока оран през юли, август и последвалите обработки
                    по-късно са от решаващо значение за успешна борба с многогодишните коренищни видове. При висока степен на
                    заплевеляване с повитица и паламида преди дълбоката оран е добре да се направи подметка на стърнището с
                    лемежни работни органи. Следжътвеният период е най-подходящото време за прилагане на тотални системни
                    листни хербициди на база глифозат и др., като се спазват всички изисквания за постигане на ефикасна и
                    икономически изгодна борба.
                </p>
                <p>
                    Предсеитбените обработки на почвата се определят в зависимост от степента и видовото заплевеляване, времето
                    на сеитба и начините на внасяне на хербицидите. За успешната борба срещу плевелите е много важно царевицата
                    да се засява в най-благоприятните срокове и колкото е възможно по-скоро след последната предсеитбена обработка.
                    След сеитба, за по-дружно поникване на царевицата, извършването на валиране е от особено значение. То спомага
                    за натрошаване на буците и за подравняване на почвената повърхност, което осигурява равномерно разпределение
                    и добра ефективност на почвените хербициди. За борба срещу едногодишни житни и двусемеделни плевели и пониците
                    от семена на многогодишни видове може да се проведе еднократно или двукратно пролетно брануване. Плевелите
                    са най-уязвими след покълнване, преди поникване и в начални фази от развитието си – от поникване до 1–2-и лист.
                    За да се ограничат повредите по културата от брануването, царевичните растения трябва да са във фенофаза
                    2–3-и лист (фенофаза на развитие 12–13). Обработката се извършва с леки щригелни брани перпендикулярно или
                    по диагонал на посоката на редовете при слънчево и топло време. Така повехналите царевични растения с намален
                    тургор по-слабо се повреждат от работните органи на браната.
                </p>
                <p>
                    Царевицата се отглежда редово, което позволява механизирани обработки в междуредията. Според типа и състоянието
                    на почвата и наличните плевели се провеждат различен брой окопавания. Окопаването трябва да започне във
                    фенофаза 4-и – 6-и лист на царевицата (фенофаза на развитие 14–16) и да приключи до 8–10 лист. За намаляване
                    на повредите по царевицата охранителната зона трябва да бъде 10–12 см от двете страни на редовете. Важен
                    етап от борбата с плевелите при царевицата е вземането на правилно решение относно химичната борба с тях
                    и по-специално дали е необходимо третиране с хербициди. Критерии за това са:
                </p>
                <p>• количество на плевелните семена в почвата преди поникване на културата и плевелите.</p>
                <p>• видов състав и степен на заплевеляване след поникване на царевицата.</p>
                <p>• фаза на развитие на плевелите и културата.</p>
                <p>
                    Изборът на хербициди или комбинации от хербициди трябва да бъде съобразен с видовия състав на наличните плевели.
                    В зависимост от механизма на действие на регистрираните хербициди при царевицата те се прилагат както почвено
                    (предсеитбено или следсеитбено), така и вегетационно в различни фази от развитието на културата. Много често
                    отделните хербициди не са достатъчно ефективни срещу всички плевели. В такива случаи се налага използването
                    на комбинации от хербициди. Хербицидите трябва да бъдат приложени правилно, при подходящи почвени и климатични
                    условия, благоприятстващи тяхното действие и постигането на добра ефективност. Много важно е определянето
                    на дозите съобразно съдържанието на органично вещество и глина в почвите. В някои страни се препоръчва
                    използването на атразин да не е повече от 150 г/дка с оглед опазване на подпочвените води от замърсяване.
