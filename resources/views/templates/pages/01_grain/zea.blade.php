                <h3 class="my_class_h3">Гниене на семената и пониците – <span class="latin_name">Pithium spp. и Fusarium spp.</span></h3>
                <h4 class="my_class_h4">Описание на болестта</h4>
                <p>
                    <span class="latin_name">Pithium spp. и Fusarium spp.</span> са почвени патогени. Те нападат царевицата от
                    периода на покълнване до фаза 3-и лист (фенофаза 10–13). Предизвикват загниване и непоникване на семената.
                    В по-късна фаза предизвикват поява на тъмнокафяви, мокри петна, които бързо се разрастват и водят до загиване
                    на растенията. В резултат на това посевите поникват неравномерно и са със слаб растеж. Болестта се среща
                    основно в години със студена, дъждовна пролет и симптомите са по-силно проявени на почви с висока влажност.
                </p>

                <h4 class="my_class_h4">Стратегия за борба</h4>
                <p>
                    Превантивните мерки включват сеитбообръщение (за намаляване на запаса от <span class="latin_name">Pithium</span>)
                    и осигуряване на добра почвена структура. Почвените гъби нападат предимно изтощени и слабо покълнали посеви.
                    Поради това срокът на засяване не трябва да е много рано напролет. Трябва да се използват устойчиви сортове
                    или третирани семена.
                </p>
                <h4 class="my_class_h4">Активни вещества</h4>
                <p>Третиране на семената: <span class="active-substance">карбоксин, тритиконазол, тирам, каптан.</span></p>
