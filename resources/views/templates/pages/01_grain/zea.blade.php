                <h3 class="my_class_h3">Специфично одобрение и поправка</h3>
                <p >Първоначално одобрена през септември 2005 г. от НСРЗ</p>
                <br/>

                <p >
                    Указанията по Добрата растителнозащитна практика при царевицата представлява част от ЕРРО-програма за изготвяне
                    на такива указания за всички основни култури от ЕРРО-региона. Те трябва да бъдат четени заедно с ЕРРО-стандарт
                    РР 2/1(1) “Принципи на добрата растителнозащитна практика”. Указанията покриват методите за контрол на
                    вредителите (в това число патогени и плевели) по царевицата (<span class="latin_name">Zea mays</span>).
                </p>
                <p >
                    Царевицата се отглежда навсякъде в ЕРРО- региона за различни цели. Използва се основно като фуражна култура
                    и за зърно, но част от производството е насочено към отглеждане на семепроизводствени посеви или отглеждането
                    на сладка царевица за зърно. Посевите, отглеждани за производство на посевен материал, не се покриват от
                    това ръководство. Трябва да се отбележи, че изискванията за границите (допустимите нива) на остатъчните
                    количества и третиранията преди прибиране на реколтата са по-строги при сладката царевица за зърно. Освен
                    това качеството на зърното е с по-голямо значение при отглеждането на сладка царевица.
                </p>
                <p >
                    ДРЗП при контрола на вредителите по царевицата може да се приеме като съвкупност от няколко елемента.
                    При тяхното използване е възможно да се постигне ефективен контрол на вредителите и в същото време да се
                    оптимизира приложението на продуктите за растителна защита:
                </p>
                <p >
                    • Семената трябва, доколкото е възможно, да бъдат свободни от болести в момента на засяване.
                </p>
                <p >
                    • Използваните сортове трябва да бъдат подходящи съответно за почвения тип и климатичните условия и за
                    предпочитане да са устойчиви или толерантни спрямо основните вредители.
                </p>
                <p >
                    • Санитарните и културно-стопански практики при царевицата са важни. Сеитбообръщението е ефективен начин
                    за поддържане ниска числеността на популациите от някои вредители. Растителните остатъци трябва да се
                    инкорпорират или заорават. Културно-стопанските практики трябва да осигуряват добра подготовка на площите
                    и поддържане на подходящ хранителен баланс. Сроковете на сеитба трябва да бъдат подбрани така, че почвените
