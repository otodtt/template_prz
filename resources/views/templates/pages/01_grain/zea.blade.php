                <h3 class="my_class_h3">Царевичен стъблопробивач – <span class="latin_name">Ostrinia nubilalis</span></h3>
                <h4 class="my_class_h4">Описание на неприятеля</h4>
                <p>
                    Царевичният стъблопробивач е пеперуда със светлокафяв до бледожълт цвят. Развива две поколения годишно,
                    като вреда по царевицата нанася второто поколение. Пеперудите от това (второ) поколение снасят яйцата си
                    на групички (до 70 броя) по долната повърхност на листата, като това съвпада с фаза изметляване. След
                    излюпването първоначално гъсениците се хранят в пазвите на листата, а след това навлизат в стъблата, където
                    дълбаят ходове. Ларвите на царевичния стъблопробивач са жълто-сиви с тъмна ивица на гръбната страна. Те
                    тунелират още и дръжките на кочаните и самите кочани, по-рядко повреждат зърната на кочаните и метлицата.
                    Растенията с повредени стъбла и метлици често се пречупват. В началото на август ларвите на царевичния
                    стъблопробивач изпадат в диапауза. Зимуват в растителните остатъци.
                </p>
                <h4 class="my_class_h4">Стратегия за борба</h4>
                <p>
                    Агротехническите мероприятия като унищожаване на растителните остатъци (т.е. на зимуващите стадии – ларвите)
                    или използването на толерантни сортове могат да ограничат популацията на неприятеля. Химичната борба цели
                    унищожаването на младите ларви в момента на тяхното излюпване и преди вгризването им в стъблата. Важно е
                    да се направи опит да бъде постигнат по-дълъг период на защита на растенията срещу неприятеля, като обикновено
                    еднократно третиране не е достатъчно да покрие целия период на излюпване и първите ларви са потенциално
                    по-опасни от тези, които са излюпени от яйцата, отложени от възрастните насекоми в края на летежа им.
                    Могат да бъдат прилагани гранулирани или течни продукти за растителна защита. Препоръчват се две третирания,
                    които се извършват по сигнал на РСРЗ. За целите на биологичния контрол се използва Trichogramma spp., което
                    също дава добри резултати. Правят се три внасяния на паразита: първото (3000–5000 бр./дка) в началото на
                    яйцеснасянето, второто (4000–5000 бр./дка) – по време на масовото яйцеснасяне, а третото – 8–10 дни по-късно
                    в доза 5000–10000 бр./дка. ИПВ е следният – до фенофаза 6–8 лист: 10 бр. яйчни групички/100растения
                    (при царевица за зърно) и 3 бр. яйчни групички/100 растения (при царевица за семепроизводство); след
                    изметляване: 90% нападнати растения с по 3–4 повреди/растение (при царевица за зърно) и 10% нападнати растения
                    с по 1–2 повреди/ растение (при царевица за семепроизводство).
                </p>
