                    бъдат насочени към изтощаване и унищожаване на подземните им органи;</p>
                <p  >
                    • правилно редуване на културите в определената площ, като монокултурното отглеждане на ечемика трябва да се избягва особено там, където разпространението на дивия овес, полска лисича опашка и др. дребносеменни плевели е масово;
                </p>
                <p  >
                    • прилагане на диференцирани предсеитбени обработки на почвата, изхождайки от характера на заплевеляване;
                </p>
                <p  >
                    • сеитба на оптимална дълбочина и в най- подходящите срокове. В случаите, когато площите са силно заплевелени с лисича опашка и други зимно-пролетни плевели, сеитбата трябва да се извърши към края на оптималния срок за района. Така се унищожават по-голям процент поници от по-рано поникнали семена с предсеитбените обработки;
                </p>
                <p >
                    • валиране на посевите непосредствено след сеитбата, което подсигурява и ефективно действие на почвените хербициди.
                </p>

                <p>
                    Хербицидите могат да бъдат прилагани предсеитбено, преди поникване, след поникване (на есен или през пролетта)
                    и преджътвено. Решението трябва да се взема конкретно, съобразно предварителна информация за състоянието
                    на заплевеляването в дадена площ. Голяма част от посевите в страната са силно заплевелени със зимно-пролетни
                    видове плевели и ефемери, които поникват основно през есента, като: полска лисича опашка, ветрушка,
                    житна пиявица, полска овсига, див мак, полска ралица, синя метличина, къклица, врабчово семе, трицветна и
                    полска теменужка, видовете подрумчета, бръшлянолистно великденче, весларка, стъблеобхватна мъртва коприва и др.
                    Тези плевели конкурират зимния ечемик в началото на неговото развитие, което влияе отрицателно и върху
                    гарнираността на посевите. Ето защо в случаите, когато се знае, че има голям запас от жизнеспособни
                    семена в почвата от посочените видове и се очаква силно заплевеляване на посевите от зимен ечемик, химичната
                    борба трябва да започне от есента с подходящи хербициди с почвено и листно действие. В късно засетите
                    посеви и посевите със слаба заплевеленост не се налага третиране преди пролетта. При висока плътност на
                    плевелите (над икономическия праг на вредност) трябва да се проведе навременно третиране с хербициди през
                    пролетта, съобразено с фазите на плевелите и културата и състава на заплевеляване. Трябва да се отбележи,
                    че биологичният и икономически праг на вредност варират в отделните случаи, защото зависят от много
                    фактори: биологичните особености на сорта, срока на сеитба, почвени и климатични условия, степен и вид
                    на заплевеляване, гъстота и фитосанитарно състояние на посева и др. ДРЗП е да се констатира, че съществуват
                    благоприятни условия за растежа на културата преди прилагането на листен хербицид и подходящи фенофази на
                    плевелите за неговата ефективност. Много важно е да се вземат внимателно решения преди третиране с даден
                    листен хербицид, като се имат предвид фазите на развитие на културата и плевелите и видовият им състав,
                    за да се избегне некомпетентна (ненавременна и безрезултатна) употреба на ПРЗ. При слабо заплевелени и
                    сравнително чисти от плевели посеви не е необходимо да се използват хербициди. В случаите, когато преобладават
                    видове плевели, устойчиви на хормоноподобните хербициди, трябва да се избере най-подходящата комбинация
                    от хербициди с добра ефективност срещу тях. Рискът от последействие на хербицидите в почвата върху
                    следващата култура в сеитбообръщението също трябва да бъде отчитан. С цел да се избягва формирането на
                    резистентност на плевелите към даден хербицид или група хербициди, е необходимо да се спазват препоръките,
                    дадени в специализирани ръководства. Борбата срещу многогодишните кореновоиздънкови плевели като паламида,
                    поветица, горуха и др. трябва да бъде съобразена с тяхната плътност, фази на развитие и ефективността на
                    хербицидите към тях. Ако в преджътвения период в площите с ечемик се развият многогодишните плевели балур,
                    паламида, поветица, тревист бъз и образуват значителна листна повърхност, в посевите може да се приложи
                    глифозат, като по това време културата е почти приключила развитието си, а зърното е близо до пълна
                    зрялост (при влажност 25-30%).
                </p>
