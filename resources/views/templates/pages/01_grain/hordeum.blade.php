                <h3 class="my_class_h3">Брашнеста мана  – <span class="latin_name">Erysiphe graminis</span></h3>
                <h4 class="my_class_h4">Описание на болестта</h4>
                <p>
                    Причинителят на брашнестата мана е специализирана форма, която напада ечемика. Симптомите се проявяват най-напред
                    по влагалищата, братята и долните листни етажи под формата на нежен, бял налеп, който по-късно става мръснобял до
                    сивкав, брашнест и повърхностен. Листата остават зелени и активни известно време след заразяването, след което
                    нападнатите тъкани постепенно загиват. Върху мицела се образуват многобройни конидиоспори, които се разпространяват
                    от вятъра на значителни разстояния и заразяват здрави растения Заразяването от конидиалната форма изисква висока
                    влажност на въздуха, а спорообразуването и разпространението на спорите се благоприятства по-скоро от сухи условия.
                    Развитието на брашнестата мана се благоприятства от редуване на сухи и влажни условия. Заразените тъкани на листата
