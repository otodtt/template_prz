                <h3 class="my_class_h3">Покрита главня - (<span class="latin_name">Ustilago hordei</span>), Праховита главня (<span class="latin_name">U. nuda</span>) </h3>
                <h4 class="my_class_h4">Описание на болестта</h4>
                <p>
                    <span class="latin_name">Ustilago hordei</span> причинява покрита главня по ечемика. Младите растения се заразяват по системен път от спори,
                    намиращи се по повърхността на семената или в почвата. Скоро след цъфтежа заразените растения образуват главниви класове. Болните растения
                    изостават в развитието, а зърната са изпълнени с черна маса от спори, покрити с прозрачна обвивка.</p>
                <p>
                    <span class="latin_name">Ustilago nuda</span> причинява праховита главня по ечемика. Заразата се пренася като мицел в семената. Гъбата прониква
                    в ендосперма, когато се формира зърното. Заразените семена дават началото на системно болни растения. Болните класове ясно се различават по
                    време на изкласяването. От влагалището на последния лист излиза изцяло превърнат в черна прашеста маса клас, покрит с тънка прозрачна ципа,
                    която се разкъсва и освобождава спорите. От класа остава само вретеното. Спорите се пренасят чрез вятъра и заразяват масово по време на цъфтеж
                    (<a class="phases_link" href="#table_bbch" target="_self" title="към фенофазите"> фенофаза 61–69</a>).</p>
                <p>И в двата случая щетите се изразяват в преки загуби от повредените класове. </p>

                <h4 class="my_class_h4">Стратегия за борба</h4>
                <p>
                    По време на жътвата обвивката на главнивите семена се разпуква и спорите се разнасят. Заразяването по този начин или от спори в почвата се
                    осъществява при покълнване на семената и може да бъде предотвратено от контактен фунгицид.
                </p>
                <p>
                    При заразяване с праховита главня семената носят заразата във вътрешността си. За да се предотврати по-нататъшното развитие на гъбата в
                    растението след покълнване на семето, е необходимо използването на системен фунгицид. Най-ефективният начин да се предпазят посевите е
                    използването на здрав и обеззаразен посевен материал. При сеитба с нетретирани семена опасността от разпространение на болестта е по-голяма,
                    поради което тази практика не е ДРЗП. Препоръчва се фитопатологичен лабораторен анализ на партидите зърно за посев за удостоверяване на
                    годността им за ползване или нуждата от третиране.
                </p>

