            <div class="col-md-12 text-justifysss text-indent">
                <h3 class="my_class_h3">Специфична сфера на действие</h3>
                <p >Този стандарт очертава принципите на добрата растителнозащитна практика при ечемика.</p>
                <h3 class="my_class_h3">Специфично одобрение и поправка</h3>
                <p >Първоначално одобрена през септември 2005 г. от НСРЗ</p>
                <br/>
                <p >
                    Ечемикът се отглежда в по-умерените части на ЕРРО-региона. Зърното и сламата се използват за храна на
                    животните, а в някои страни значителна част от зърното служи за производство на малц, бира и различни преработени
                    продукти. Ечемикът се отглежда в сеитбообращение с други зърнени или полски култури.
                </p>
                <p >
                    Посевите от ечемик се засяват през есента или пролетта. Пролетните култури са изложени на действието на
                    вредители за по-кратък период. Семената може да се купуват сертифицирани или несертифицирани, но могат да бъдат
                    произвеждани и директно от фермерите. Редуването на културите с други зърнено-житни или полски култури
                    намалява плътността на популациите на вредители в почвата или в растителните остатъци. Обработката на
                    почвата следва да се провежда диференцирано в зависимост главно от предшественика и степента на заплевеляване.
                    Минималните културно-стопански практики като директна сеитба, чрез които могат да се намалят разходите за труд,
                    благоприятстват съхранението и по-масовото намножаване на популации от вредители в почвата. Тъй като ечемикът се
                    отглежда основно за зърно, целта на защитата от вредители е да осигури добро качество и количество на получената реколта. Физичните и химичните характеристики на зърното са особено важни, когато се предвижда преработката му.
                </p>
                <p >
                    Използването на устойчиви сортове и здрав посевен материал, сеитба в оптимални срокове, прилагане на
                    правилно сеитбообръщение, осигуряване на добре подготвено легло на семената са важни елементи на ДРЗП при ечемика.
                    Химически обработки с ПРЗ могат да се наложат във всяка фенофенофаза от развитието на културата.
                    Третиране на семената срещу семенните патогени с продукти за растителна защита е ДРЗП, когато е извършено
                    срещу тези, които не могат да бъдат контролирани с листни фунгициди. ДРЗП може да бъде и третирането на
                    семената срещу други вредители, ако то ще замени няколко третирания рано през вегетацията и по този начин ще се
                    намали употребеното количеството на продуктите за растителна защита. Продуктите, използвани за третиране на
                    семената, трябва да са ефективни срещу възможно по-голям брой гъбни патогени или неприятели, представляващи
                    интерес. Важно е семената да бъдат равномерно покрити с ПРЗ.
                </p>
                <p >
                    Едновременното прилагане на две или повече активни вещества под формата на пръскане или третиране на семената е
                    ДРЗП, ако вредителите, срещу които те са ефективни, наистина се срещат или се очаква нападение от тях.
                    Фермерите и консултантите трябва да познават основните вредители, да обследват редовно полетата и да ползват
                    максимално критериите за прогноза и сигнализация, оповестяващи употребата на ПРЗ при достигане праговете на вредност.
                    Дозите трябва да отговарят на комплекса от наблюдаваните вредители, вземайки под внимание страничните ефекти и възможните взаимодействия.
                </p>
                <p >
                    ДРЗП е да се намали течът и нежеланото разпиляване на продуктите за растителна защита. Това може да се постигне
                    чрез използване на противокапкови устройства към всички разпръсквачи, предпазващи от теч, или на оборудване,
                    което произвежда добър и еднакъв капков спектър при всички дюзи по дължината на щангата на пръскачката.
                </p>
                <p >
                    Рискът да се развие резистентност спрямо фунгициди, инсектициди и хербициди е реална заплаха. ДРЗП е да се
                    избегне пръскането с фунгицид или инсектицид по-късно през вегетацията, ако активно вещество със същия начин на
                    действие вече е било използвано при третиране на семената. Препоръчително е използването на активно вещество с
                    друг начин на действие. За борба срещу икономически важните болести праховита главня и ръжди трябва да се
                    редуват активни вещества и смесени формулации, съдържащи продукти с различен механизъм на действие,
                    доколкото това практически е възможно.
                </p>

