                    придават на тялото черен цвят. По тази особеност ларвата прилича на пиявица, откъдето неприятелят е получил името си.
                </p>
                <p>
                    Житните пиявици имат едно поколение в годината и презимуват като възрастни насекоми под растителните остатъци
