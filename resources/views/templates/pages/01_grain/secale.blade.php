                    половина на май във фенофаза вретенене. Женските снасят яйцата си по горната, по-рядко по долната страна
                    на листата поединично или на верижки от 2 до 10 броя, най-често 2–4. Ларвите се излюпват в края на
                    вретененето на ръжта, но масовото им развитие е по време на изкласяването и цъфтежа.
                </p>
                <p>
                    Вредят възрастните насекоми и ларвите. Бръмбарите изгризват двата епидермиса и паренхима на листата във
                    вид на тесни и дълги прозорчета между нерватурата, а ларвите унищожават само горния епидермис и паренхима.
                    Повредените листа побеляват и при силно нападение изсъхват, развитието на растенията се забавя, а добивът
                    от зърно намалява. Повредите от житната пиявица са най-често на хармани, но при масово нападение може да
                    бъде унищожен целият посев.
                </p>
                <h4 class="my_class_h4">Стратегия за борба</h4>
                <p>
                    Основно значение за опазване на посевите от житните пиявици има химичната борба, която може да се проведе
                    на един или два етапа (срещу възрастните насекоми или ларвите). У нас няма утвърдени прагове на вредност
                    за житните пиявици при ръжта, затова трябва да се спазват указанията на специалистите от РСРЗ. Препоръчва
                    се също минерално торене и умерено азотно подхранване на посевите през пролетта, с което се повишава
                    естествената устойчивост на растенията.
