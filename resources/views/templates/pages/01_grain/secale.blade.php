                <h3 class="my_class_h3">Листен пригор – <span class="latin_name">Rhynchosporium secalis</span></h3>
                <h4 class="my_class_h4">Описание на болестта</h4>
                <p>
                    <span class="latin_name">Rynchosporium secalis</span> е широко разпространен листен патоген. Той се съхранява
                    в растителните остатъци. Симптомите се изразяват в поява на продълговати, надлъжни, в началото синьо-зелени,
                    а по-късно сиви петна, оградени с тъмнокафяв венец. Петната се появяват най-напред по долните листа. При
                    влажно време от долната страна на листа се образува сивкавобелезникав налеп. Болестта се проявява още през
                    есента и се разпространява на хармани през периода на вретенене
                    (<a class="phases_link" href="#table_bbch" target="_self" title="към фенофазите">фенофаза 30–39</a>).
                </p>
                <h4 class="my_class_h4">Стратегия за борба</h4>
                <p>
                    Сортовете са с различна степен на чувствителност. Трябва да се унищожават самосевките, житните плевели и
                    растителните остатъци. Късната сеитба намалява опасността от болестта. Степента на проява трябва да бъде
                    отчетена рано напролет и при необходимост да се проведат едно или две третирания. Това важи особено за
                    райони с по-влажни условия. Да се следят указанията на РСРЗ.
                </p>
