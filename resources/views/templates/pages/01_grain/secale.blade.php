            <div class="pest-description-container col-md-10 text-justifysss text-indent">
                <h3 class="my_class_h3">Мораво рогче – <span class="latin_name">Clavicps purpurea</span></h3>
                <h4 class="my_class_h4">Описание на болестта</h4>
                <p>
                    Гъбата паразитира върху голям брой житни култури и диви житни треви. Ръжта е особено чувствителна към
                    този патоген. Признаците се проявяват по време на цъфтеж, като се засягат само цветчетата. От тях започва
                    да изтича жълтеникава течност в продължение на няколко дни. След това в тях вместо зърна се образуват
                    роговидни, тъмночервени склероции. Те обикновено падат върху почвата преди жътва. В други случаи се
                    прибират заедно със зърното и при сеитбата отново попадат в почвата. През следващата година по време на
                    цъфтеж склероциите покълнват в почвата с кълн, върху който се образуват перитеции и аскоспори. Склероциите
                    съдържат токсини, преди всичко алкалоиди, които са опасни за здравето на хората и животните. Аскоспорите
                    се разнасят от дъжда, насекомите и вятъра и заразяват цъфтящите растения.
                </p>

                <h4 class="my_class_h4">Стратегия за борба</h4>
                <p>
                    Повечето сортове са чувствителни. Устойчивостта на сортовете е свързана с продължителността на цъфтене и
                    степента на отваряне на цветовете. Патогенът притежава специализирани форми – раси. Най-важното мероприятие
                    за борба е използването на чисти от зараза семена за сеитба. Това се постига, като посевният материал се
                    пречиства чрез флотационен метод (използване на разтвор от натриев хлорид). Дълбоката оран (25 см) и
                    сеитбообръщението са ефикасни мерки за борба. При това склероциите се заравят в почвата, не покълнват и
                    загиват на следващата година. Борбата срещу плевелите, особено срещу
                    <span class="latin_name">Alopecurus myosuroides</span> (лисича опашка),е важна мярка. Третирането на
                    семената с триадиминол потиска покълнването на склероциите. Не се води химична борба при полски условия.
                </p>


