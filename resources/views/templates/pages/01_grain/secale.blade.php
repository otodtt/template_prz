                    (отделя химични вещества, които потискат останалите растителни видове). Ето защо в посевите от ръж много
                    често не се налага извеждането на химична борба. Въпреки всичко при слабо гарнирани и недобре развити посеви
                    може да се наложи прилагането на химична борба, особено срещу проблемни плевели като ветрушка
                    (<span class="latin_name">Apera spicaventi</span>), полска лисича опашка
                    (<span class="latin_name">Alopecurus myosuroides</span>), обикновена лепка
                    (<span class="latin_name">Galium aparine</span>), трирога лепка (<span class="latin_name">Galium tricorne</span>)
                    и др. Третирането може да се извърши предпоникващо или по време на вегетацията на ръжта. Решението трябва
                    да се взема конкретно, съобразно предварителна информация за състоянието на заплевеляването в дадената площ.
                    При висока плътност на плевелите (над икономическия праг на вредност) трябва да се проведе навременно
                    пръскане с хербициди. Изборът на отделния хербицид се извършва съобразно вида и степента на заплевеляване.
                    Трябва да се отбележи, че биологичният и икономическият праг на вредност варират в отделните случаи, защото
                    зависят от много фактори: биологичните особености на сорта, срока на сеитба, почвени и климатични условия,
                    вид и степен на заплевеляване, гъстота и фитосанитарно състояние на посева и др. ДРЗП е да се констатира,
                    че съществуват благоприятни условия за растеж на културата преди прилагането на листен хербицид и подходящи
                    фенофази на плевелите за неговата ефективност. Много важно е да се вземат прецизни решения преди третиране
                    с даден листен хербицид, като се имат предвид фазите на развитие на културата и плевелите и видовият им
                    състав, за да се избегне некомпетентна (ненавременна и безрезултатна) употреба на ПРЗ. С цел да се избягва
                    формирането на резистентност на плевелите към даден хербицид или група хербициди е необходимо да се спазват
                    препоръките, дадени в специализирани ръководства. Борбата срещу многогодишните кореновоиздънкиви плевели
                    като паламида, поветица, горуха и др. трябва да бъде съобразена с тяхната плътност, фази на развитие и
                    ефективността на съответния хербицид към тях.
                </p>
