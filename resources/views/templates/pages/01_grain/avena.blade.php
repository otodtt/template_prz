        <div class="col-md-12 text-justifysss text-indent">
            <h3 class="my_class_h3">Специфична сфера на действие</h3>
            <p >Този стандарт очертава принципите на добрата растителнозащитна практика при овес.</p>
            <h3 class="my_class_h3">Специфично одобрение и поправка</h3>
            <p >Първоначално одобрена през септември 2005 г. от НСРЗ</p>
            <br/>
            <p >
                Указанията за Добра растителнозащитна практика при овеса представляват част от ЕРРО-програмата за изготвяне
                на такива указания за всички основни култури от ЕРРО- региона. Те трябва да бъдат четени заедно с
                ЕРРО-стандарт РР 2/1(1) Принципи на добрата растителнозащитна практика. Указанията включват методите за
                контрол на вредителите по овеса (<span class="latin_name">Avena sativa</span>).
            </p>
            <p >
                Овесът се отглежда в по-умерените и скандинавските части на ЕРРО-региона. Зърното се използва основно
                като фураж, в частност като храна за коне. В средиземноморските страни овесът се отглежда за зелен фураж.
                Незначителна част от зърното се използва за консумация от хората под формата на тестени храни, овесена каша и мюсли.
            </p>
            <p >
                Овесът се засява през пролетта. Минималните културно-стопански практики като директна сеитба могат да
                намалят разходите за работна сила, но благоприятстват преживяването и създаването на популации от вредители
                в почвата. Тъй като овесът се отглежда основно за зърно, целта на защитата от вредители е да осигури добро
                количество на получената реколта. За да се осъществи това, важно е посевът да не е полегнал и жътвата да
                се извърши навреме и в кратки срокове.
            </p>
            <p >
                Овесът се разглежда като добър предшественик в схемата за сеитбообръщение на културите. Той в значителна
                степен е устойчив на черното кореново гниене (<span class="latin_name">Gaeumannomyces graminis</span>) и
                гниенето на основите и паразитно полягане (<span class="latin_name">Tapesia yallundae</span>),
                като спомага за намаляване на риска от тези болести в сеитбообръщението.
                Опасността от нематоди трябва да бъде преценена преди сеитбата на овеса в случай, че бъдат отглеждани
                чувствителни сортове.
            </p>
            <p >
                Използването на устойчиви сортове и здрав посевен материал, сеитба в оптимални срокове, спазване на правилно
                сеитбообращение, създаване на добре подготвено легло на семената са важни елементи на ДРЗП при овеса.
                Третирания с ПРЗ могат да се наложат във всяка фенофаза от развитието на културата, но малко са ПРЗ,
                прилагани при овеса. Третирането на семената е ДРЗП, когато то е извършено срещу вредители, които не могат
                да бъдат контролирани с листни фунгициди.
            </p>
            <p >
                Продуктите, използвани за третиране на семената, трябва да покриват възможно по-голям комплекс от гъбни
                или насекомни вредители, представляващи интерес. Важно е семената да бъдат равномерно и еднакво покрити с ПРЗ.
            </p>
            <p>
                Едновременното прилагане на две или повече активни вещества за третиране на семената е ДРЗП, ако вредителите,
                срещу които те са ефективни, наистина се срещат или се очаква нападение от тях. Фермерите и консултантите
                трябва да познават основните вредители, да обследват редовно полетата, да ползват максимално критериите за
                прогноза, оповестяващи употребата на ПРЗ при достигане праговете на вредност. Дозите трябва да отговарят
                на спектъра от наблюдаваните вредители, вземайки под внимание индивидуалните ефекти и възможните взаимодействия.
                В частност за гъбните болести ДРЗП е да се избират продукти и срокове на приложение по оптимален начин.
            </p>
            <p>
                ДРЗП е да се намали течът и нежеланото разпиляване на продуктите за растителна защита. Това може да се
                постигне чрез използване на противокапкови устройства към всички разпръсквачи, предпазващи от теч, или на
                оборудване, което произвежда добър и еднакъв капков спектър при всички дюзи по дължината на щангата на пръскачката.
            </p>
