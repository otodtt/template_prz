            <h3 class="my_class_h3">Листна, коронеста ръжда по овеса  – <span class="latin_name">Puccinia coronata</span></h3>
            <h4 class="my_class_h4">Описание на болестта</h4>
            <p>
                Овесът се напада от специализираната форма <span class="latin_name">Puccinia coronata</span>
                съществуват и патотипове, които са специализирани спрямо някои устойчиви сортове. Напролет овесът се заразява
                от пренасяни по въздушен път ецидиоспори, образувани върху междинните гостоприемници от род
                <span class="latin_name">Rhamnus</span> – у нас най-често <span class="latin_name">R. cathartica</span>
                (зърнастец), или чрез въздушнопреносими уредоспори от южните на нас райони. Епидемии от ръжди по зърнено-житните
                се развиват при вторично заразяване от уредоспори. Коронестата ръжда образува широки, оранжево-жълти пустули
                с брашнеста консистенция от уредоспори с размери, вариращи между 1–5 мм, неравномерно разпръснати по листната
                повърхност. В края на вегетацията се образуват телейтоспори, с които патогенът продължава развитието си върху
                междинните гостоприемници.
            </p>
            <h4 class="my_class_h4">Стратегия за борба</h4>
            <p>
                Съществуват редица културно-стопански практики, които могат да ограничат заразяването на овеса. На първо
                място, да бъдат отглеждани устойчиви сортове или да се избягват силно чувствителните. Самосевките от овес
                да се унищожават. Трябва да се избягва прекомерното азотно торене и гъста сеитба.
            </p>

