            <h3 class="my_class_h3">Праховита главня по овеса - (<span class="latin_name">Ustilago avenae</span>)</h3>
            <h4 class="my_class_h4">Описание на болестта</h4>
            <p>
                В началото на изметляване от влагалището на последния лист се показва по-малка и сбита метлица, чиито зърна
                и плеви са превърнати изцяло в черна, праховидна, главнива маса от спори. Тези спори се разпрашават по време
                на цъфтежа и заразяват новообразуващите се семена на съседните растения. Болестта се разпространява чрез
                вятъра или по време на вършитбата. По време на покълнването на семената младите поници се заразяват по
                системен път от спорите, намиращи се под люспите или по повърхността на семената.
            </p>

            <h4 class="my_class_h4">Стратегия за борба</h4>
            <p>
                Семената на овеса могат да бъдат заразени повърхностно от <span class="latin_name">U. аvenae</span> при
                наливане на зърното и по време на жътвата. Някои сортове проявяват пълна устойчивост. Известно е, че при
                чувствителните сортове системните фунгициди предотвратяват развитието на гъбата в растенията след покълнване.
                За да се избегне развитието на болестта, важно е да се използват чисти, сертифицирани семена или такива,
                обеззаразени с фунгициди. И двата метода са много ефективни при контрола на болестта.
            </p>
