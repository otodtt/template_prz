            <h3 class="my_class_h3">Покрита главня - (<span class="latin_name">Ustilago hordei f. sp. аvenae</span>)</h3>
            <h4 class="my_class_h4">Описание на болестта</h4>
            <p>
                По време на фенофаза изметляване зърната в болните метлици са превърнати в сравнително компактна, черна
                главнива маса. В основата на зърната се вижда тънка, прозрачна ципа, която покрива главнивата маса от спори.
                Плевите и осилите са по-слабо засегнати. Спорите се разпрашават по време на вършитба и полепват по здравите
                семена или попадат между плевите и зърната. От повърхностно заразените семена се развиват системно заразени поници.
            </p>
            <h4 class="my_class_h4">Стратегия за борба</h4>
            <p>
                Сортове овес проявяват различия в чувствителността спрямо тази болест. За да се избегне развитието на болестта,
                важно е да се използват свободни от зараза, сертифицирани семена или такива, третирани с фунгициди. И двата
                метода са много ефективни при контрола на болестта.
