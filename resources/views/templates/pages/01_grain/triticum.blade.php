                <h4 class="my_class_h4">Описание на неприятеля</h4>
                <p>
                    Житните стъблени оси имат едно поколение годишно, което обхваща две календарни години и
                    зимуват като ларва в пашкул в следжътвените остатъци непосредствено под повърхността на
                    почвата. Осите започват да излитат от края на април и началото на май във фенофаза
                    вретенене, а масовият им летеж е по време на изкласяването и цъфтежа на пшеницата.
                    Те имат блестящочерно тяло, дълго от 5,5 до 12 мм, с голяма черна глава и дълги нишковидни
                    антени.</p>
                <p>
                    Женските оси снасят яйцата си в стъблата, изключително в междувъзлието под класа на
                    пшеницата. Ларвите имат S-образно тяло, със светлокафява кръгла глава и без крака. Напълно
                    развити достигат дължина 8–18 мм. Хранят се с паренхимната тъкан на стъблата и до края на
                    млечната зрелост на пшеницата прогризват постепенно всички калена на стъблото. След III
                    възраст ларвите правят и по-дълбоки нагризвания, които засягат част от големите проводящи
                    снопчета. В резултат на тази повреда зърната не се изхранват нормално, което води до
                    намаляване на абсолютното им тегло, съпроводено и с известно влошаване на качеството.
                    В края на восъчната зрелост на пшеницата ларвите завършват развитието си, достигат основата
                    и на нивото на почвената повърхност прегризват кръгообразно стъблото отвътре. Незасегнат
                    остава само епидермисът, който се прегризва частично. Прегризаните стъбла не се забелязват
                    и при нормални условия продължават да се крепят върху корена си. С настъпване на пълната
                    зрелост при силен вятър или дъжд те масово се пречупват, полягат или падат на земята, което
                    води до загуба на зърно при жътвата. Намалението на добива от прегризаните и полегнали
                    стъбла е по-голямо при по-редките посеви и късна жътва.</p>
                <h4 class="my_class_h4">Стратегия за борба</h4>
                <p>
                    Борбата срещу житната стъблена оса включва основно агротехнически мероприятия, които в
                    определена степен ефективно ограничават числеността й. Най-голямо значение сред тях има
                    дълбочината на следжътвената обработка на почвата. Добра растителнозащитна практика е да
                    се извърши дълбока оран на стърнището (от 23 до 35 см), последвана от пролетно култивиране
                    с 29 брануване на дълбочина до 18 см. Препоръчва се осигуряване на нормална гъстота на
                    посевите, отглеждане на устойчиви сортове, спазване на сеитбообращение, по-ранна жътва.
                    Осите имат слаба летателна способност и посевите, отдалечени от миналогодишни стърнища, се
                    нападат по-слабо.</p>
                <p>
                    Химическата борба има ограничено значение за регулиране числеността на
                    стъблените оси и е насочена срещу възрастните насекоми. Оптималният срок за провеждането е
                    фенофенофаза изкласяване на пшеницата и праг на вредност 3 бр./кв.м.</p>
