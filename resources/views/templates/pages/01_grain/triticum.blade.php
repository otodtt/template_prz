        <div class="row">
            <div class="pest-description-container col-md-10 text-justifysss text-indent">
                <h3 class="my_class_h3 phase">Хесенска муха – <span class="latin_name">Mayetiola destructor Say;</span></h3>
                <h3 class="my_class_h3 phase">Житна тревна муха – <span class="latin_name">Opomyza florum Fab;</span></h3>
                <h3 class="my_class_h3 phase">Черна пшеничена муха – <span class="latin_name">Phorbia fumigata Meigen (синоним Phorbia securis Tien);</span></h3>
                <h3 class="my_class_h3 phase">Черна пролетна пшеничена муха – <span class="latin_name">Phorbia haberlandti Schiner (синоним Phorbia penicillifera Jermy).</span></h3>
                <h4 class="my_class_h4">Описание на неприятеля</h4>
                <p>
                    <span class="bold">Хесенска муха:</span> Възрастните насекоми са дребни, ръждивосиви
                    комарчета, с дълги антени и дължина на тялото 2,5 – 3,5 мм. Краката са дълги, по-тъмно оцветени.
                    Ларвите са със сплеснато-вретеновидна форма, млечнобели, с надлъжна зеленикава ивица по гърба
                    и дължина 4 мм.</p>
                <p>
                    Хесенската муха има две поколения в годината (пролетно и есенно) и зимува като напълно развита
                    ларва в пупариум в повредените житни растения. Мухите от пролетното поколение летят през април,
                    а тези от есенното – през септември и снасят яйцата си по горната повърхност на листата по
                    няколко заедно.</p>
                <p>
                    <span class="bold">Житна тревна муха:</span> Тялото й е тънко, удължено, с
                    ръждивожълт цвят и дължина 3,5–4 мм. Главата е полусферична с дълги четинки на темето.
                    Преднегръдът е с два реда тъмни четинки, разположени отстрани на средната линия.
                    Крилата са жълтеникави, с кафяви петна на напречните жилки и затъмнени върхове.</p>
                <p>
                    Житната тревна муха има едно поколение в годината. Прелита в пшеничените посеви след
                    поникването им през есента във фенофенофаза 1–2 лист, където копулира и снася яйцата
                    си, които остават да зимуват. Ларвите се излюпват рано напролет, през втората половина
                    на март или първата и втората десетдневка на април. Те са прозрачно бели в I и
                    кремавожълти във II и III възраст, с вретеновидна форма и дължина до 8 мм.</p>
                <p>
                    <span class="bold">Черна пшеничена муха и черна пролетна пшеничена муха:</span>
                    Възрастните насекоми са средноголеми, черни мухи със сиво-кафяв налеп и дълги космици
                    на гърдите и краката и дължина на тялото около 4–5 мм. Крилата са тъмни с тъмнокафяви
                    жилки. Ларвата в III възраст е жълто-бяла, с почти цилиндрична форма, леко по-тънка в
                    предния край и дължина около 8 мм. <span class="bold">Черната пшеничена муха</span> има две поколения
                    (пролетно и есенно) в годината и зимува като ларва и какавида в стъблата на нападнатите
                    житни растения и в почвата. <span class="bold">Черната пролетна пшеничена муха</span> има едно поколение (пролетно)
                    и зимува като какавида в почвата. Летежът и на двата вида мухи започва рано напролет,
                    още в първите топли дни на февруари–март. Мухите от есенното поколение на черната
                    пшеничена муха летят през септември и октомври и снасят яйцата си по есенните житни
                    посеви. Женските снасят яйцата си по стъблата на младите растения, които по това време
                    са във фенофаза 2-и–3-и лист-братене.</p>
                <p>
                    Вредят ларвите, които се хранят с върха на нарастване на младите стъбла, вследствие на
                    което централният им лист постепенно завяхва, пожълтява и изсъхва. След като ларвите
                    завършат развитието си, стъблата загиват. Повредите от мухите са особено опасни в
                    години с неблагоприятни за развитието на пшеницата климатични условия. Тогава голяма
                    част от нападнатите растения загиват, без да се развият допълнителни братя, и посевите
                    силно оредяват.</p>
                <p>
                    Повредените в по-късна фенофаза пшеничени стъбла от хесенската муха се изкривяват, не
                    образуват класове или класовете формират по-малък брой и с понижено тегло зърна.
                    Посевите с по-висока степен на нападение изглеждат като бити от градушка.</p>
                <h4 class="my_class_h4">Стратегия за борба</h4>
                <p>
                    Рискът от повреди е най-висок при посевите, засети през втората половина на септември и
                    в началото на октомври. В това отношение като най-благоприятен срок за сеитба на
                    пшеницата в отделните райони на страната с оглед извеждането на борбата с мухите се
                    препоръчват втората или третата десетдневка на октомври. ДРЗП е извършване подметка на
                    стърнищата и есенна дълбока оран, комбинирано минерално торене или умереното азотно
                    подхранване на посевите през пролетта, с което се повишава естествената устойчивост на
                    растенията. ДРЗП е унищожаване на самосевките и използване на устойчиви към
                    хесенската муха сортове пшеница.</p>
                <p>
                    Преди да се вземе решение за химическа борба срещу мухите, е необходимо да се
                    обследват посевите. Най-подходящият срок за определяне на плътността им е фенофенофаза
                    3-и лист на пшеницата за житната тревна муха и сигнализиране на пръскането при праг на
                    вредност 3 бр./кв.м и фенофази поникване – 2-и–3-и лист – начало на братене за есенните
                    поколения на хесенската и черната пшеничена мухи при праг на вредност 3 бр./кв.м
                    Обследването на посевите за определяне необходимостта от пръскане срещу пролетните
                    поколения на черните пшеничени мухи следва да бъде извършено в първите топли дни на
                    февруари – март, а срещу пролетното поколение на хесенската муха – през април.</p>
                <h4 class="my_class_h4">Активни вещества</h4>
                <p>Хесенска муха: <span class="active-substance">фипронил, метомил, тиаметоксам.</span><br/>
                    Житна тревна муха, черни пшеничени мухи: в страната няма регистрирани инсектициди до настоящия момент.</p>
