                    действие на почвените хербициди.</p>

                <p>Хербицидите могат да бъдат прилагани предсеитбено, преди
                    поникване, след поникване (на есен или през пролетта) и преджътвено. Решението
                    трябва да се взема конкретно, съобразно предварителна информация за състоянието на
                    заплевеляването в дадената площ. Голяма част от посевите в страната са силно
                    заплевелени със зимно-пролетни видове плевели и ефемери, които поникват основно
                    през есента, като: полска лисича опашка, ветрушка, житна пиявица, полска овсига,
                    див мак, полска ралица, синя метличина, къклица, врабчово семе, трицветна и полска
                    теменужка, видовете подрумчета, бръшлянолистно великденче, весларка, стъблеобхватна
                    мъртва коприва и др. Тези плевели конкурират зимната пшеница в началото на нейното
                    развитие, което влияе отрицателно и върху гарнираността на посевите. Ето защо в
                    случаите, когато се знае, че има голям запас от жизнеспособни семена в почвата от
                    посочените видове и се очаква силно заплевеляване на посевите от пшеница, химичната
                    борба трябва да започне от есента с подходящи хербициди с почвено и листно
                    действие. В късно засетите посеви и посевите със слаба заплевеленост не се налага
                    третиране преди пролетта. При висока плътност на плевелите (над икономическия праг
                    на вредност) трябва да се проведе навременно третиране с хербициди през пролетта
                    съобразено с фазите на плевелите и културата и състава на заплевеляване. Трябва да
                    се отбележи, че икономическият праг на вредност варира в отделните случаи, защото
                    зависи от много фактори: биологичните особености на сорта, срока на сеитба, почвени
                    и климатични условия, вид и степен на заплевеляване, гъстота и фитосанитарно
                    състояние на посева и др. ДРЗП е да се констатира, че съществуват благоприятни
                    условия за растежа на културата преди прилагането на листен хербицид и подходящи
                    фенофази на плевелите за неговата ефективност. Много важно е да се вземат прецизни
                    решения преди третиране с даден листен хербицид, като се имат предвид фенофазите на
                    развитие на културата и плевелите и видовият им състав, за да се избегне
                    некомпетентна (ненавременна и безрезултатна) употреба на ПРЗ. При слабо заплевелени
                    и сравнително чисти от плевели посеви не е необходимо да се прилагат хербициди.
                    В случаите, когато преобладават видове плевели, устойчиви на хормоноподобните
                    хербициди, трябва да се избере най-подходящата комбинация от хербициди с добра
                    ефективност срещу тях. Рискът от последействие на хербицидите в почвата върху
                    следващата култура в сеитбообръщението също трябва да бъде отчитан. С цел да се
                    избягва формирането на резистентност на плевелите към даден хербицид или група
                    хербициди е необходимо да се спазват препоръките, дадени в специализирани
                    ръководства. Борбата срещу многогодишните кореновоиздънкови плевели като паламида,
                    поветица, горуха и др. трябва да бъде съобразена с тяхната плътност, фази на
                    развитие и ефективността на хербицидите към тях. Ако в преджътвения период в
                    площите с пшеница се развият многогодишните плевели балур, паламида, поветица,
                    тревист бъз и растенията им надраснат посевите, може да се приложи глифозат,
                    като по това време културата е почти приключила развитието си, а зърното е близо
                    до пълна зрялост (при влажност 25–30%).</p>
