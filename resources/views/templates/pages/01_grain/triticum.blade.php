                <h3 class="my_class_h3">Специфична сфера на действие</h3>
                <p >Този стандарт очертава принципите на добрата растителнозащитна практика при пшеницата.</p>
                <h3 class="my_class_h3">Специфично одобрение и поправка</h3>
                <p >Първоначално одобрена през септември 2005 г. от НСРЗ</p>
                <br/>

                <p >
                    Указанията по Добрата растителнозащитна практика при пшеницата представляват част
                    от ЕРРО-програмата за изготвяне на такива указания при всички основни култури
                    от ЕРРО-региона. Те трябва да бъдат четени заедно с ЕРРО-стандарт
                    РР 2/1(1) – “Принципи на добрата растителнозащитна практика”.
                    Указанията включват методите за контрол на вредителите (в това число болести, неприятели и плевели)
                    по пшеницата (<span class="latin_name">Triticum ssp.</span>), включително меката
                    (<span class="latin_name">Triticum aestivum</span>) и твърдата (<span class="latin_name">Triticum durum</span>) пшеница.</p>
                <p >
                    Пшеницата се отглежда в повечето части на ЕРРО-региона.
                    Зърното се използва за консумация от хората и животните.
                    Сламата също може да служи като храна на животните.
                    Пшеницата се отглежда при редуване с други зърнени или полски култури.
                    Посевите от пшеница се засяват през есента. Сеитбообръщението с други зърненожитни или
                    полски култури намалява плътността на популациите от вредители в почвата или в
                    растителните остатъци. Минималните културно-стопански практики чрез които могат да се
                    намалят разходите по отглеждането, благоприятстват преживяването и увеличават
                    числеността на популациите от вредители в почвата. Тъй като пшеницата се отглежда
                    основно за зърно, целта на защитата от вредители е да се получи максимално количество и
                    качество на добива. Физичните и химични характеристики на зърното са особено важни,
                    когато то е предназначено за преработка.</p>
                <p >
                    Използването на устойчиви сортове и здрав посевен материал, сеитба на пшеницата
                    в оптимални срокове, правилно сеитбообращение, осигуряване на добре подготвено легло на
                    семената са важни елементи на ДРЗП при пшеницата. Сортовете с добра устойчивост спрямо
                    полягане трябва да се използват в районите, където полягането е основен проблем.</p>
                <p >
                    Третирания с продукти за растителна защита (ПРЗ) могат да се наложат във всяка
                    фенофенофаза от развитието на културата. Третирането на семената е ДРЗП, когато се
                    извършва срещу вредители, които не могат да бъдат контролирани през вегетацията.
                    ДРЗП може да бъде и третиране на семената срещу други вредители, ако то ще замени няколко
                    третирания и по този начин ще се намали количеството продукти за растителна защита рано
                    през вегетацията. Пшеницата е по-толерантна спрямо почвени вредители, в сравнение например
                    с царевицата или захарното цвекло, защото повредените млади стъбла могат да бъдат
                    възстановени до определена степен по време на братенето. Продуктите, използвани за
                    третиране на семената, трябва да покриват възможно по-голям спектър от гъбни или насекомни
                    вредители. Важно е семената да бъдат равномерно покрити с ПРЗ.</p>
                <p >
                    Едновременното прилагане на две или повече активни вещества под формата на
                    третиране на семената или през вегетацията е ДРЗП, ако вредителите, срещу които те са
                    ефективни, наистина се срещат или се очаква нападение от тях. Фермерите и консултантите
                    трябва да познават основните вредители, да обследват редовно полетата и да ползват
                    максимално бюлетините за прогноза на вредителите, оповестяващи употребата на ПРЗ при
                    достигане праговете на вредност.</p>
                <p >
                    Дозите на прилагания продукт трябва да отговарят на комплекса от
                    наблюдаваните вредители в момента на приложение, отчитайки индивидуалната ефективност и
                    възможните взаимодействия.</p>
                <p >
                    Третирането на посевите с ПРЗ е ДРЗП, когато се извършва ултрамалообемно или
                    с технически изправна техника. ДРЗП е да се намали течът и нежеланото разпиляване на
                    продуктите за растителна защита. Това може да се постигне чрез използване на противокапкови
                    устройства към всички разпръсквачи, предпазващи от теч, или на оборудване, което произвежда
                    добър и еднакъв капков спектър при всички дюзи по дължината на щангата на пръскачката.</p>
                <p >
                    Рискът да се развие резистентност спрямо фунгициди, инсектициди и хербициди е
                    реална заплаха. ДРЗП е да се избегне пръскането с фунгицид или инсектицид по-късно през
                    вегетацията, ако активно вещество със същия механизъм на действие вече е било използвано
                    при третиране на семената. Препоръчително е използването на активно вещество с друг
                    механизъм на действие. За борба срещу икономически важните болести праховита главня и
                    ръжди трябва да се редуват активни вещества и смесени формулации, съдържащи продукти с
                    различен механизъм на действие, доколкото това практически е възможно.</p>
            </div>
