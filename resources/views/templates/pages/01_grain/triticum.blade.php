                <h3 class="my_class_h3">Обикновен житар – <span class="latin_name">Anisoplia austriaca Hrbst</span></h3>
                <h4 class="my_class_h4">Описание на неприятеля</h4>
                <p>
                    В житните ни посеви вредят няколко вида житари, сред които най-разпространен е
                    обикновеният житар. Възрастните насекоми имат овално тяло с дължина 12–15 мм, оцветено
                    черно с металнозелен оттенък. От горната страна е голо, а от долната покрито със
                    сиво-бели космици. Твърдите крила на обикновения житар са червено-кафяви с черно
                    четириъгълно петно около щитчето, от което започва тъмна линия по вътрешния им край.
                    Житарите имат двегодишен цикъл на развитие. Зимуват ларвите, които живеят в почвата в
                    продължение на 22–23 месеца. Бръмбарите започват да излитат от края на май и първата
                    половина на юни. Те са топлолюбиви и светлолюбиви насекоми. Активни са в топли и
                    слънчеви дни от 8–9 ч. сутринта до смрачаване.</p>
                <p>
                    За житните ни посеви повредите от ларвите на житарите са без съществено икономическо
                    значение. Основната вреда при тях нанасят бръмбарите, които изгризват неузрелите зърна, а
                    друга част оронват на земята по време на храненето си. Предпочитат най-много пшеницата.
                    Възрастните житари живеят около месец и за този период изяждат 7–8 гр. зърна, а заедно с
                    оронените зърна количеството на унищожените класове достига 9–10 бр. В години на
                    по-масовото им намножаване загубите в добива достигат до 20–30%, а в отделни случаи – и
                    до 50%.</p>
                <h4 class="my_class_h4">Стратегия за борба</h4>
                <p>
                    Житарите се намножават масово по-често в площи с лоша агротехника при неспазване на
                    сеитбообращение и неправилна система на обработка на почвата. Ето защо ДРЗП е да се
                    редуват културите и да се прилагат различни схеми за обработка на почвата, с които
                    ефективно може да се намали числеността на яйцата, ларвите и какавидите. Препоръчва се да
                    се извърши подметка на стърнището на дълбочина 10–14 см и ранна дълбока оран, проведени
                    непосредствено след жътвата. Посевите с пшеница, нападнати във висока степен от житари,
                    на следващата година трябва да бъдат заети от окопни култури.</p>
                <p>
                    За да се вземе решение за химическа борба срещу възрастните житари, е необходимо да се
                    обследват посевите, което се извършва по указание на РСРЗ. Най-подходящият срок за
                    определяне плътността на бръмбарите и сигнализиране на пръскането са фенофазите
                    цъфтеж-наливане на зърното при праг на вредност 4–5 бр./кв.м и в млечно- восъчната
                    зрелост на пшеницата при праг на вредност 6–8 бр./кв.м. Третирането трябва да се извърши
                    в сутрешните или следобедни часове на тихи, топли и слънчеви дни, когато активността на
                    бръмбарите е по-слаба и действието на продуктите се проявява по-ефективно.</p>
                <h4 class="my_class_h4">Активни вещества</h4>
                <p><span class="active-substance">Есфенвалерат + фенитротион.</span></p>
