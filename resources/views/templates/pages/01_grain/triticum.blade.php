                    третиране на семената с фунгициди. Когато климатичните условия благоприятстват появата на
                    гниене по класовете, трябва да бъдат извършени предпазни пръскания с фунгициди.
                    Пръсканията с фунгициди, извършени срещу други болести, контролират и гниенето по
                    класовете.</p>
