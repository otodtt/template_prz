        <div class="aa-title" id="tilletia-and-ustilago">
            <span></span>
        </div>
        <div class="row">
            <div class="pest-description-container col-md-10 text-justifysss text-indent">
                <h3 class="my_class_h3">Главни – <span class="latin_name">Tilletia levis, Tilletia tritici, Ustilago tritici</span></h3>
                <h4 class="my_class_h4">Описание</h4>
                <p>
                    <span class="latin_name bold">Tilletia levis и Tilletia tritici</span> (синоними <span class="latin_name">T. foetida</span> и <span class="latin_name">T. caries</span>)
                    са причинители на твърдата (обикновена; мазна) главня по пшеницата. Вторият вид се среща
                    по-рядко у нас. Младите растения се заразяват системно от спорите, намиращи се по
                    повърхността на семената. Болестта може да се запазва и в почвата, но за условията в
                    нашата страна това няма практическо значение. След поникване на семената мицелът на
                    патогена се развива успоредно с развитието на растението, следвайки вегетационния връх,
                    и скоро след цъфтежа достига до класa. Болните класове са синкавозелени, класчетата
                    надебеляват, осилите и плевите се разперват. Болните растения може да са с изостанал
                    растеж, а зърната са изпълнени с маса от черни спори, които са мазни на пипане и имат
                    миризма на развалена риба. Болестта може да причини до 10-20% загуби в добива.</p>
                <p>
                    <span class="latin_name bold">Ustilago tritici</span> е причинител на праховитата главня по
                    пшеницата. Заразата се пренася със семената. Гъбата прониква във вътрешността на семената,
                    достигайки ендосперма по време на формиране на зърното. Заразените семена водят до системна
                    форма на проява на болестта. Болните класове личат добре след изкласяване. От влагалището на
                    най-горния лист излиза изцяло разрушен и превърнат в главнива маса клас. Спорите са черни,
                    разпрашават се и се разнасят свободно от вятъра. От класа остава само вретеното. Когато
                    попаднат върху цветовете на здравите растения, става заразяване на новообразуващите се зърна.
                    По правило процентът на загуба от добива е равен на процента заразени класове в посева.</p>
                <h4 class="my_class_h4">Стратегия за борба</h4>
                <p>
                    Пшеничените семена се заразяват повърхностно от <span class="latin_name bold">Tilletia levis</span>
                    и <span class="latin_name bold">T. tritici</span> по време на жътвата, когато обвивката на
                    главнивите зърна се разкъсва и спорите се разпиляват. В този случай заразяването се осъществява по
                    време на покълнването и може да бъде предотвратено от контактен фунгицид. При нападение от праховита
                    главня заразяването е станало по време на цъфтежа и с жътвата патогенът е вече във вътрешността на
                    семената. Това налага използването на системен фунгицид, за да се предотврати по-нататъшното развитие
                    на гъбата в растението след покълнването на семената. И в двата случая е важно да се използват здрави
                    (свободни от зараза) и третирани с фунгицид семена.</p>
                <p>
                        Обеззаразяването на семената преди сеитба е
                    основен, много ефективен метод за борба с тези болести. Лабораторното изследване на партидите семена е
                    ДРЗП за преценка необходимостта от третиране срещу семенно преносими патогени.</p>
