                <p  >• срещу многогодишните коренищни и кореновоиздънкови плевели обработките трябва да
                    бъдат насочени към изтощаване и унищожаване на подземните им органи.</p>
                <p  >• монокултурното отглеждане на пшеницата трябва да се избягва.
                    ДРЗП е правилно редуване на културите в определени площи, особено там, където има
                    силно заплевеляване с див овес, полска лисича опашка и др. дребносеменни плевели.</p>
                <p  >• прилагане на диференцирани предсеитбени обработки на почвата, изхождайки от
                    характера на заплевеляване;</p>
                <p  >• сеитба на оптимална дълбочина и в най-подходящите срокове. В случаите, когато
                    площите са силно заплевелени с лисича опашка и други зимно-пролетни плевели, сеитбата
                    трябва да се извърши към края на оптималния є срок за района. Така се унищожават
                    по-голям процент поници от по-рано поникнали семена чрез предсеитбените обработки.</p>
