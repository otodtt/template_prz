                    <span class="latin_name">Fusarium culmorum. Gibberella zeae</span> зимува в растителните остатъци, където формира
                    перитеции. Формираните в тях аскоспори се разпространяват по въздуха и осъществяват
                    заразявания на пролет. Заразяването с <span class="latin_name">Fusarium culmorum</span> става чрез конидиоспори,
