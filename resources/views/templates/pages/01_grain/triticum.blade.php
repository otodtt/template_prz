                <h3 class="my_class_h3">Полягане</h3>
                <h4 class="my_class_h4">Описание</h4>
                <p>
                    При определени условия (високи норми азот, висока гъстота на посева) пшеницата е
                    склонна към полягане. По-слабите посеви може да не се повлияят от неблагоприятните
                    въздействия, но податливите на полягане сортове може да бъдат засегнати, което води до
                    неравномерно узряване и понижение качествата на зърното. Затрудненото прибиране на
                    полегналата пшеница с комбайни намалява добива. Заплевеляването води до допълнителни
                    трудности при жътвата и увеличава разходите (за просушаване на зърното). Важно е да
                    бъде сведено до минимум полягането и в частност ранното, което причинява
                    най-големи щети. Полягането може да бъде ограничено по различни начини, включително и
                    чрез прилагането на растежни регулатори.</p>
                <p>
                    Има доказателства, че някои растежни регулатори могат да подобрят развитието на
                    кореновата система, като влияят върху броя на братята и броя на образуваните класове,
                    когато са приложени в ранните фази от развитие на културата. Съществуват схващания, че
                    по този начин се подобрява добивът.</p>
                <h4 class="my_class_h4">Основна стратегия</h4>
