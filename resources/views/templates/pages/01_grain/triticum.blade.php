                <h3 class="my_class_h3 phase">Голям житен бегач – <span class="latin_name">Z. spinipes F.</span></h3>
                <h4 class="my_class_h4">Описание на неприятеля</h4>
                <p>
                    Възрастните на обикновения житен бегач са бръмбари с продълговато, смолисто черно тяло,
                    с метален блясък и дължина 14 – 16 мм. Ларвата е продълговата, мръснобяла, с тъмнокафяви
                    петна от горната страна на коремните сегменти. Главата и трите гръдни сегмента са
                    тъмнокафяви. Напълно развита достига дължина 25 мм. Големият житен бегач е с дължина на
                    тялото 18–21 мм. Ларвата прилича на тази на обикновения житен бегач, но е с по-голяма
                    дължина. Житните бегачи имат едно поколение в годината и зимуват като ларви в почвата на
                    нападнатите житни посеви.</p>
                <p>
                    Вредят ларвите и възрастните насекоми, но значение за добива имат главно повредите от
                    ларвите, развитието на които обхваща есенния и раннопролетния период от вегетацията на
                    пшеницата. Вредната им дейност започва от покълнването на житните растения, на които
                    прегризват кълновете под земята, а след поникването прегризват листата и младите стъбла,
                    сдъвкват ги и изсмукват сока, без да засягат жилките. Повредените растения изсъхват и се
                    навиват във вид на малки туфички, подобни на дреб от кълчища, които са най-сигурният
                    белег за присъствието на тези неприятели. Ларвите на житните бегачи живеят в почвата в
                    ходове, които изкопават близо до растенията на дълбочина 25 – 30 см. След като унищожат
                    растенията близо до хода си, ларвите се придвижват на друго място, където правят нови
                    ходове. Повредите от тях обикновено са на хармани, най-често в местата, заплевелени с
                    пирей. На тези места силно повредените растения загиват и в посевите се образуват петна.
                    Ако нападението е по-слабо, посевът оредява, а при масова поява могат да бъдат унищожени
                    всички растения. Ларвите завършват развитието си през първата или втора половина на май.</p>
                <p>
                    Бръмбарите от новото поколение се появяват през юни и се срещат до късно през есента.
                    Активни са след залязване на слънцето, когато се покачват по класовете и се хранят със
                    зърната, като ги нагризват. Част от тях оронват на земята, но повредите от възрастните
                    насекоми са незначителни в сравнение с тези, причинени от ларвите.</p>
                <h4 class="my_class_h4">Стратегия за борба</h4>
                <p>
                    Основно значение за ограничаване на риска от нападение от житните бегачи има спазването
                    на правилно сеитбообращение, като не се допуска засяването на пшеницата след житни
                    предшественици. Ако това изискване е невъзможно да се изпълни, то тогава е необходимо да
                    се извърши колкото е възможно по-рано и без загуби прибиране на реколтата, почистване
                    на площите от сламата, подметка на стърнището и дълбока оран и унищожаване на самосевките
                    около 20 дни преди сеитбата. За намаляване на риска от нападение от тези неприятели
                    допринасят и всички културно-стопански мерки, които подпомагат по-бързото развитие на
                    растенията.</p>
                <p>
                    ДРЗП е химичната борба да се провежда срещу ларвите чрез внасяне на гранулати
                    заедно със сеитбата на пшеницата или чрез третиране на посевите по време на вегетацията.
