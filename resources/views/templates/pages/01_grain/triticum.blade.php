        <div class="aa-title" id="eurygaster-integriceps">
            <span></span>
        </div>
        <div class="row">
            <div class="pest-description-container col-md-10 text-justifysss text-indent">
                <h3 class="my_class_h3 phase_bottom">Вредна житна дървеница – <span class="latin_name">Eurygaster integriceps Put.</span>;</h3>
                <h3 class="my_class_h3 phase">Австрийска дървеница – <span class="latin_name">E. austriaca Schr.</span>;</h3>
                <h3 class="my_class_h3 phase">Мавърска дървеница – <span class="latin_name">E. maura L.</span>;</h3>
                <h3 class="my_class_h3 phase">Остроглави дървеници - <span class="latin_name">Aelia acuminata L.; A. rostrata Boh.</span></h3>
                <h4 class="my_class_h4">Описание на неприятеля</h4>
                <p>
                    Възрастните дървеници от род <span class="latin_name">Eurygaster</span> имат
                    продълговато тяло със заоблен изпъкнал гръб, светлокафяв цвят и дължина 9–13 мм.
                    Ларвите преминават 5 възрасти в развитието си, които морфологично се различават една
                    от друга. Остроглавите дървеници от род <span class="latin_name">Aelia</span> са с удължено-яйцевидна форма,
                    светложълт цвят и дължина 8–9 мм. Най-опасна за пшеницата е вредната житна дървеница
                    <span class="latin_name">Eurygaster integriceps</span>, която е доминиращ вид в основните зърнопроизводителни
                    райони у нас.</p>
                <p>
                    Житните дървеници имат едно поколение в годината и зимуват като възрастни насекоми
                    под растителните остатъци в покрайнините на горите. В пшеничените посеви през
                    пролетта те прелитат обикновено през втората и трета десетдневка на април, хранят се
                    в продължение на 2–3 седмици, след което женските започват да снасят. Разполагат
                    яйцата си на купчинки от 12–14 броя, наредени в два или три реда, по растителните
                    остатъци, листата и класовете. Непосредствено след снасянето яйцата са светлозелени,
                    а в края на ембрионалното си развитие придобиват седефен блясък с характерна червена
                    рисунка с форма на котва. Излюпването на ларвите съвпада с цъфтежа или края на
                    цъфтеж на пшеницата през май, в зависимост от климатичните условия. Ларвите от първа
                    възраст не се хранят и до преминаването им във втора възраст остават събрани на
                    купчинка върху хориона на яйцата или близо до тях. Развитието на ларвите от втора до
                    пета възраст съвпада с фенофазите млечна-пълна зрелост на пшеницата. От втората
                    половина на юни или първата десетдневка на юли започва окрилянето на възрастните
                    от новото поколение. Те се хранят още около 10–15 дни, след което мигрират в
                    местата на зимуване.</p>
                <p>
                    Възрастните дървеници и ларвите вредят, като смучат сок – възрастните от стъблата,
                    листата, класовете и зърната на пшеницата, а ларвите – само от зърната. В мястото на
                    убождането те вкарват ферменти, които превръщат белтъчините в зърната и в сока на
                    растенията в разтворима форма, годна за изсмукване и усвояване от дървениците.
                    Частта от стъблата, листата и класовете над мястото на повреда загива, което води
                    до разреждане на посевите и намаляване на добивите.</p>
                <p>
                    При ранна миграция на дървеницата в посевите, висока плътност, сухо и топло време
                    повредите от възрастното могат да са значителни. В по-късна фенофаза на пшеницата
                    повредите по класовете от възрастните дървеници се нарича
                    “частично” или “пълно белокласие”.</p>
                <p>
                    Зърната, повредени от ларвите в млечна зрелост, остават спаружени.
                    В мястото на повредата във восъчна зрелост те са по-плоски или вдлъбнати, а при
                    зрелите зърна в мястото на убождането се образува тъмно петно с по-светъл ореол
                    около него. Най-много убождания по зърната причиняват ларвите от IV и V възраст и
                    възрастните от новото поколение. Повредените зърна имат влошен глутен, по-ниско
                    абсолютно и хектолитрово тегло и намалена кълняемост, а брашното от тях е с влошени
                    хлебопекарни качества.</p>
                <h4 class="my_class_h4">Стратегия за борба</h4>
                <p>
                    Голямо значение за намаляване на риска от повреди от житните дървенци има химичната борба.
                    Основната стратегия за борба е насочена срещу презимувалите дървеници след миграцията в
                    посевите или срещу ларвите втора – трета възраст при достигане прага на вредност. За да се
                    прецени необходимостта от употребата на инсектициди, много важно е да се следват сигналите
                    на регионалните служби за растителна защита (РСРЗ) за провеждане на обследванията и
                    указанията за определяне плътността на възрастните и ларвите.</p>
                <p>
                    За икономически праг на вредност у нас е приета следната плътност: за презимувалите
                    дървеници – 2 бр./кв.м при суха и топла пролет, и 4 бр./кв.м при хладна пролет и гъсти
                    посеви във фенофенофаза братеневретенене и съотношение мъжки към женски дървеници 1:1.
                    При ларвите прагът на вредност е 2 бр./кв.м при благоприятни климатични условия и
                    нормален посев, и 4 бр./кв.м – при хладна пролет и гъсти посеви през периода на млечната
                    зрелост, когато 30% от ларвите са в III възраст.</p>
                <p>
                    Необходимо е да се знае, че тенденция за увеличаване опасността от житните дървеници
                    съществува в години с умерено студена зима и достатъчна снежна покривка, ранна и топла
                    пролет с количество на валежите под нормата, топло лято с температури над нормата и с
                    по-малко количество на валежите през първата му половина. В такива години презимувалите
                    дървеници мигрират в посевите дружно, по-рано и причиняват много повреди по младите
                    растения. Опасността от тях е особено голяма за късно засетите, редки и изостанали във
                    фенологичното си развитие посеви. Това налага внимателно да се следят бюлетините на
                    РСРЗ за състоянието на неприятеля в съответствие с конкретната климатична обстановка.</p>
                <p>
                    Ранното и в съкратени срокове прибиране на реколтата (10–15 дни) е ДРЗП за ограничаване
                    числеността на дървениците. Останалите върху стърнищата новоокрилени възрастни не
                    успяват да се доизхранят и през зимата значителна част от тях загиват.</p>
                <p>
                    За намаляване плътността на ларвите важна роля имат и яйчните паразити – теленомусите,
                    които са водещи сред ентомогфагите и през отделните години причиняват 30-40 до 80%
                    смъртност на яйцата. Заразените яйчни купчинки имат черна или сиво-черна окраска.
