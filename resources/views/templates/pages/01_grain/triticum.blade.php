                    (<a class="phases_link" href="#table_bbch" target="_self" title="към фенофазите">фенофази 49 – 59</a>).</p>
                <p class="phase_bottom">
                    Сигнал за третиране срещу брашнестата мана се дава при достигане
                    на икономическия праг на вредност, който е както следва:</p>
