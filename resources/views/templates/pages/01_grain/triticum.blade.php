                <p class="phase">
                    <a class="phases_link" href="#table_bbch" target="_self" title="към фенофазите">Фенофази – 55–69</a> 30%
                    степен на нападение на листната маса и първи пустули върху флагелния лист.</p>
                <p>
                    Ако брашнестата мана се среща заедно с други болести, добра растителнозащитна практика е
