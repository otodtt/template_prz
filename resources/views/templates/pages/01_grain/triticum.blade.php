                <h3 class="my_class_h3">Брашнеста мана по пшеницата – <span class="latin_name">Erysiphe graminis f. sp. tritici</span></h3>
                <h4 class="my_class_h4">Описание на болестта</h4>
                <p>
                    Симптомите на болестта са повърхностни, брашнести, бели налепи по
                    листата, листните влагалища и класовете. Листата остават зелени и активни известно
                    време след заразяването, след което нападнатите тъкани постепенно загиват.
                    Конидиоспорите се образуват в големи количества по повърхността на нападнатите тъкани и
                    изглеждат като бели прашести туфи. Те се разпространяват от вятъра на големи разстояния
                    и причиняват заразявания. Заразяването с конидиоспори изисква висока въздушна влажност
                    (но не и капка вода върху листната повърхност), а спорообразуването и разпространението
                    на спорите се благоприятства по-скоро от сухите условия. Дъждът и капките вода по листата
                    ограничават спорообразуването и нарастването на мицела. Развитието на брашнестата мана се
                    благоприятства от редуване на сухи и влажни условия. Заразените тъкани на листата стават
                    хлоротични и престават да фотосинтезират. Ранната форма на болестта се проявява по изоставащите
                    млади растения, а по-късната форма намалява активната повърхност на листата, вследствие на което
                    добивът на зърно е редуциран. Умерената степен на нападение от брашнеста мана е допустима.
                    Във фенофаза от поява на лигулата на флагелния лист до края на цъфтежа
                    (<a class="phases_link" href="#table_bbch" target="_self" title="към фенофазите">фенофази 39 – 65</a>),
                    върху застаряващия мицел могат да се появят клейстотеции (плодни тела) под формата на черни точици.</p>

                <h4 class="my_class_h4">Стратегия за борба</h4>
                <p>
                    Пшеницата се напада само от специализираната форма
                    <span class="latin_name">tritici</span> на <span class="latin_name">E. graminis.</span>
                    Причинителите набрашнеста мана по ечемика или ръжта не могат да заразят пшеницата и обратно. По принцип пшеницата
                    не се напада така силно както ечемикът, но щетите могат да бъдат значителни, ако не се води борба.
                    Съществуват редица практики, които до известна степен могат да намалят заразяването с брашнеста мана.
                    Препоръчва се отглеждането на устойчиви сортове. По-редките пшеничени посеви намаляват разпространението
                    на брашнеста мана. Ранната сеитба, високата гъстота на посева и обилното азотно торене благоприятстват
                    разпространението на болестта.</p>
                <p>
                    Критичните моменти от развитието на пшеницата по отношение на заразяването с
                    брашнеста мана е периодът братене до появата на класа
                    (<a class="phases_link" href="#table_bbch" target="_self" title="към фенофазите">фенофази 21 – 59</a>)
