                    умело прилагане на всички предпазни и агротехнически мероприятия, които включват:</p>
                <p  >• използването на чист посевен материал;</p>
                <p  >• унищожаване на източниците на заплевеляване преди осеменяване на плевелите в
                    необработваемите площи, крайпътните ивици, около иригационните съоръжения и др.;</p>
                <p  >• навременно и качествено прибиране на реколтата преди оронването на плевелните семена;</p>
                <p  >• почистване на прибиращата техника при преминаване от едно поле в друго;</p>
                <p  >• диференцирани обработки на почвата съобразно степента и видовото заплевеляване на
                    отделните площи. Наложително е 32 силно заплевелените площи да се изорават на по-голяма
