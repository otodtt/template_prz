                <h3 class="my_class_h3 phase">Синя житна пиявица – <span class="latin_name">Lema lichenis Voet.</span></h3>
                <h4 class="my_class_h4">Описание на неприятеля</h4>
                <p>
                    Възрастните насекоми на обикновената житна пиявица имат продълговато, почти цилиндрично
                    тяло с дължина 4–5 мм. Преднегръдът е сферичен, жълтооранжев, а твърдите
                    крила – тъмносини с надлъжни точкувани ивици. Тялото на синята житна пиявица е изцяло
                    интензивно синьо и с дължина 3,5–4 мм. Ларвите имат тъмна глава и светложълто тяло,
                    силно изгърбено в задната част и покрито със слузеста материя. В нея плуват
                    екскрементите, които придават на тялото черен цвят. По тази особеност ларвата прилича
                    на пиявица, откъдето неприятелят е получил името си.</p>
                <p>
                    Житните пиявици имат едно поколение в годината и презимуват като възрастни насекоми
                    под растителните остатъци в храстите и полетата с многогодишни житни треви. В
                    пшеничените посеви мигрират масово през април или първата половина на май във
                    фенофенофаза вретенене. Ларвите се излюпват в края на вретененето на пшеницата, но
                    масовото им развитие е по време на изкласяването и цъфтежа.</p>
                <p>
                    Вредят възрастните насекоми и ларвите. Бръмбарите изгризват двата епидермиса и
                    паренхима на листата във вид на тесни и дълги прозорчета между нерватурата, а ларвите
                    унищожават само горния епидермис и паренхима. Повредените листа побеляват и при силно
                    нападение изсъхват, развитието на растенията се забавя, а добивът от зърно намалява.
                    Повредите от житната пиявица са най-често на хармани, но при масово нападение може да
                    бъде унищожен целият посев.</p>
                <h4 class="my_class_h4">Стратегия за борба</h4>
                <p>
                    Основно значение за опазване на посевите от житните пиявици има химическата борба.
                    Тя може да се проведе на един или два етапа по указания на специалистите от РСРЗ.
                    Третирането е икономически оправдано, когато се сигнализира при достигане прага на
                    вредност, който за възрастните насекоми е 30 бр./кв.м за редки посеви и суха пролет и
                    50 бр./кв.м за гъсти посеви и хладна пролет. За ларвите прага на вредност е 2 бр./стъбло
                    или 20% унищожена листна маса. За намаляване броя на житните пиявици в пшеничените посеви
                    могат да се прилагат и културно-стопански практики, като например пролетно засяване на
                    ивици с овес или пролетен ечемик около тях. Пролетните култури са предпочитана храна и
                    привличат бръмбарите, с което се улеснява провеждането на химическата борба. Препоръчва
                    се също балансирано минерално торене и умерено азотно подхранване на посевите през
                    пролетта, с което се повишава естествената устойчивост на растенията.</p>
