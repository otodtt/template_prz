                    зелени класчета. Силно нападнатите класове остават стерилни, а по-слабо нападнатите
                    образуват спаружени и набръчкани семена. При влажно време се образува розов плесенов налеп.
                    Гниенето на класовете води до намаляване на добива, понижаване на абсолютното тегло и
                    намаляване на качествените показатели на зърното. В условията на неправилно съхранение
                    заразеното зърно образува токсини, опасни за здравето на хора и животни. У нас се срещат и
                    други видове <span class="latin_name">Fusarium spp.</span> като:
