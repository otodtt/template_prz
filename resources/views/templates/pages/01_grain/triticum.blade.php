                <h3 class="my_class_h3">Пшеничен трипс – <span class="latin_name">Haplothrips tritici Kurd</span></h3>
                <h4 class="my_class_h4">Описание на неприятеля</h4>
                <p>
                    Пшениченият трипс има едно поколение в годината и презимува като ларва в почвата на
                    нападнатите пшеничени посеви. Възрастните трипси са дребни насекоми, с тънко
                    продълговато тъмнокафяво до черно тяло и дължина 1,2 – 2,2 мм. Крилата са прозрачни,
                    тесни, с дълги реснички. Непосредствено след излюпването ларвите са жълтозелени, няколко
                    часа след това стават бледочервеникави, а след първото линеене придобиват яркочервена
                    окраска. Дължината на тялото им е 1,4 – 1,8 мм. Повредите причиняват възрастните
                    насекоми и ларвите, които смучат сок от листата и класовете на пшеницата. Възрастните
                    трипси вредят във фенофазите вретенене–изкласяване–цъфтеж на пшеницата. По време на
                    вретененето те се намират под последния обвивен лист на класа, а след изкласяването се
                    концентрират по-масово по неизкласилите още стъбла. Частта от класа в мястото на
                    смученето (най-често връхната) побелява, остава недоразвита и класчетата не формират
                    зърна или образуват слаби, спаружени зърна. Основната вреда причиняват ларвите. Отначало
                    те смучат сок от плевите и цветните части, а по-късно от различни места на зърната в
                    млечна и восъчна зрелост, но най-често се струпват около “браздичката” на семето.
                    Мястото на хранене по зърното побелява и става грапаво, а “браздичката” се разширява,
                    удълбочава и придобива жълто-кафява окраска. Повредените зърна имат шуплив вид, по-ниско
                    тегло и влошени посевни качества.</p>
                <h4 class="my_class_h4">Стратегия за борба</h4>
                <p>
                    Преди организиране на химическата борба с пшеничения трипс е необходимо да се определи
                    средната плътност на неприятеля на едно стъбло или клас, като се следват указанията на
                    специалистите от РСРЗ. Оптималният период за отчитането й при възрастните насекоми е
                    по време на изкласяването, а при ларвите през периода на млечната зрелост. ДРЗП е
                    пръскане с инсектицид само в посевите, в които средна плътност е над прага на вредност:
                    за възрастните – 10 трипса/1 стъбло, за ларвите – 20 броя/1 клас (при суха пролет)
                    или 30 броя/1 клас (при влажна пролет). Плътността на пшеничения трипс ефективно
                    ограничават и редица културно-стопански мерки. На първо място това са правилното
                    сеитбообращение, като не се допуска засяване на пшеницата след житни предшественици
                    и подметката на стърнищата непосредствено след жътвата. Пшениченият трипс вреди повече
                    по онези растения, които по едни или други причини закъсняват в своето развитие, затова
                    и всички фактори, спомагащи дружното поникване и по-бързо развитие на посевите
