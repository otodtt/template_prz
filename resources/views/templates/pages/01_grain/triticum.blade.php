                    придобиват червено-кафяв цвят. При засушаване и силно нападение растенията могат и
                    напълно да изсъхнат. След изкласяването въшките смучат сок от вретеното и класчетата, в
                    резултат на което растенията формират по-малък брой и недохранени зърна, с намалено
                    абсолютно тегло. Числеността им при масова поява може да достигне до 100 и повече броя
                    на 1 клас.</p>
                <p>
                    <span class="bold">Обикновената житна въшка</span> се среща в по-ниска численост по пшеницата. Безкрилите
                    партеногенетични женски имат овално- продълговато, светлозелено тяло, с по-тъмна зелена
                    ивица на гърба. Дължината им е до 2 мм. Соковите тръбички са светли, с по-тъмни крайчета.
                    Въшките смучат сок основно от горната и долна страна на листата. При масово нападение
                    образуват големи колонии, които изцяло покриват листата, а понякога и влагалището.
                    Повредените листа избледняват, пожълтяват и загиват.</p>
                <p>
                    И двата вида листни въшки са немигриращи и презимуват като яйце по есенните посеви и
                    дивите житни треви. Ларвите се излюпват в края на март и през април и до средата на
                    восъчната зрелост на пшеницата развиват 4-6 поколения. Масовото им намножаване обхваща
                    фенофазите изкласяване-цъфтеж-млечна зрелост. С настъпване на восъчната зрелост плътността на
                    листните въшки рязко намалява вследствие загрубяване на растенията и непригодността им
                    за храна на въшките. През есента от самосевките и дивите житни треви те отново прелитат в
                    пшеничените посеви. Освен пряката вреда двата вида листни въшки вредят и косвено чрез
                    образуването на чернилни гъбички и като вектори на вируса на жълтото ечемичено в
                    джуджаване.</p>
                <h4 class="my_class_h4">Стратегия за борба</h4>
                <p>
                    Унищожаване на самосевките, сеитба на пшеницата в оптималните за отделните райони
                    срокове, избягване на прекомерното и едностранно азотно торене са ефективни
                    културно-стопански практики за ограничаване числеността на въшките и повредите от тях.
                    Числената динамика на листните въшки се регулира много успешно от комплекса хищни и
                    паразитни насекоми, сред които най-голямо значение имат възрастните и ларвите на
                    калинките и ларвите на сирфидните мухи. Затова е желателно химична борба с въшките да се
                    провежда само в посевите, в които плътността на неприятеля е над икономическия праг на
                    вредност, като се използват по-слабо отровни и с по-кратко последействие инсектициди.
                    Оптималният срок за провеждане на борбата са фенофазите от изкласяване до начало на
                    млечна зрелост. За икономически праг на вредност у нас е приета плътност 10 въшки/1
                    растение във фенофаза 2 – 3-и лист-братене през есенния период и 30 – 40 въшки/1 клас във
                    фенофаза изкласяване до начало на млечна зрелост. При организиране на химичната борба с
                    листните въшки е необходимо да се има предвид, че в големите масиви с пшеница
                    разпространението на неприятелите се ограничава в периферията им и достига най-често
                    до 150 – 200 м от края на посева.</p>
                <h4 class="my_class_h4">Активни вещества</h4>
                <p>В България няма регистрирани инсектициди до настоящия момент.</p>
