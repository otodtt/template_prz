        <div class="row">
            <div class="pest-description-container col-md-10 text-justifysss text-indent" >
                <h3 class="my_class_h3">Склеротийно гниене – <span class="latin_name">Sclerotinia sclerotiorum</span></h3>
                <h4 class="my_class_h4">Описание на болестта</h4>
                <p>
                    Ранната проява на болестта е свързана със загиване на кълновете и пониците. Нападнатите органи (стъбла,
                    разклонения, листа и бобове) загниват, отначало тъканите са воднистозелени, постепенно придобиват охрен
                    цвят, при влажно време се покриват с бял памуковиден мицел, който образува черни, полусферични грапави
                    склероции. Загнилите листа стават кафяви.
                </p>
                <p>
                    Стопанското значение на склеротийното гниене по граха в България се определя не само от преките щети, които
                    нанася болестта, но и от включването на тази култура в повечето ротационни схеми. Гъбата има голям брой
                    гостоприемници-фасул, грах, бакла, слънчоглед, домати, пипер, моркови и др.
                </p>
                <p>
                    <span class="latin_name bold">S. sclerotiorum</span> няма конидийна форма. Склероциите варират по големина
                    и съхраняват гъбата в болните растителни остатъци и почвата. Заразяването и развитието на болестта се
                    благоприятства от влажни и хладни (20-25°С) условия по време на цъфтеж. Разпространението на гъбата през
                    вегетацията се осъществява чрез дъждовните и поливни води и при допир на здрави до заразени тъкани.

                </p>
                <h4 class="my_class_h4">Стратегия за борба</h4>
                <p>
                    Спазването на 3- до 4-годишен сеитбооборот с житни и други ненападащи се от гъбата култури е основното
                    средство за борба със склеротийното увяхване. Добрата гарнираност, както и използването на сортове с по-рехава
                    листна маса и изправен храст, създават условия за по-добро проветряване на посевите, а оттук и за
                    предотвратяване развитието и разпространението на болестта. Не са известни устойчиви сортове.
                </p>
                <h4 class="my_class_h4">Активни вещества</h4>
