                <h3 class="my_class_h3">Брашнеста мана – <span class="latin_name">Erysiphe pisi=E. Polygoni</span></h3>
                <h4 class="my_class_h4">Описание на болестта</h4>
                <p>
                    Повърхността на нападнатите органи (листа, стъбла, бобове) се покриват с белезникави, плесенови налепи.
                    Тъканите под налепа остават зелени за дълъг период от време, но впоследствие се образуват кафяви, некротични
                    петна. Измежду брашнестия налеп може да се появят единични или многобройни, тъмни, точковидни плодни тела
                    (клейстотеции).
                </p>
                <p>
                    Гъбата се съхранява в растителните остатъци под форма на клейстотеции. Масово развитие на болестта се наблюдава
                    във фаза наливане на бобовете и се благоприятства от топли и сухи условия през деня, съчетани с висока
                    относителна влажност през нощта.
                </p>

                <h4 class="my_class_h4">Стратегия за борба</h4>
                <p>
                    Добрата растителнозащитна практика изисква ранна сеитба, използване на устойчиви сортове и третиране на
                    посевите с фунгициди.
                </p>
