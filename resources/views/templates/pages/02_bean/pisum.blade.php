                <h4 class="my_class_h4">Описание на болестта</h4>
                <p>
                    Напетняването на листата и бобовете при граха се причинява от три фитопатогенни гъби със сходни признаци.
                    <strong class="latin_name">A. pisi</strong> причинява напетняване на листата и бобовете, като формира кръгли
                    до овални черни петна с по-светъл център, в който се разполагат черни плодни тела (пикнидии). При
                    <strong class="latin_name">M. pinodes</strong> петната са дребни, тъмнокафяви с неправилна форма. В центъра им
                    се образуват черни плодни тела (пикнидии). Болестта се развива бързо при топли и влажни условия.
                    <strong class="latin_name">P. medicagines var. pinodella</strong> причинява напетняване в основата на стъблото
                    в близост до почвената повърхност. Петната по листа, бобове и стъбла са тъмнокафяви и некротични. Болните
                    растения са хлоротични с потиснат растеж.
                </p>
                <p>
                    Патогените се съхраняват и пренасят със семената на грах. Запазват се в болните растителни остатъци, които
                    са източник на инфекция в началото на вегетацията.
                </p>
                <h4 class="my_class_h4">Стратегия за борба</h4>
                <p>
                    Добрата растителнозащитна практика изисква да се използват здрави семена; устойчиви сортове; спазване на
                    сеитбообращение (2-3 г.); обеззаразяване на посевния материал.
                </p>

