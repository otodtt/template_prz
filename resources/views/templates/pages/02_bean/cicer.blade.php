                    Възрастното насекомо е бръмбар с продълговато-овално тяло, покрито със светложълтеникави до тъмнокафяви космици.
                    Покривните крила са правоъгълни, със заоблени върхове и не покриват пигидиума. Дължината на тялото е от 2
                    до 4,2 mm. Яйцето е млечнобяло, прозрачно, удължено. Ларвата е дъговидно извита, белезникава, младата е с
                    крака, а възрастната - без крака, дължина от 2,8 до 3,8 mm. При наши условия неприятелят не зимува на открито.
                </p>
                <p>
                    Фасулевият зърнояд напада семената както на полето, така и в складовете. Главна повреда причинява на семената
                    в складовете и домакинствата.
                </p>
                <p>
                    Възрастните женски снасят яйца само по зрелите семена в бобовете, като прогризват отвори по дължината на
                    шевовете им, в които снасят яйцата си, или през пукнатини, ако намерят такива. В складовете и домакинствата
                    те снасят яйца върху семената или между тях. Младите ларви се вгризват най-често в мястото, където се допират
                    две зърна или зърното опира до стената на боба. Входните отвори са малки, кръгли и при сортове с необагрена
                    семенна обвивка са трудно забележими с невъоръжено око, а при сортове с обагрена обвивка те се забелязват
                    като малки бели точици. След навлизане в семената ларвите се хранят с вътрешността им, като издълбават
                    тесни ходове, които впоследствие разширяват и удълбочават. Семената се изпълват с ларвни кожици и екскременти,
                    което ги прави негодни за консумация и фураж. В едно зърно се развиват до 20 и повече ларви. При силно
                    нападение остава само семенната обвивка. Напълно развитите бръмбари прогризват кръгли отвори на семенната
                    обвивка, през които излизат. Неприятелят се внася в складовете със семената, където се размножава целогодишно
                    и развива 3-4 поколения, които се застъпват.
                </p>
                <p>
                    Заразяването на полето става предимно от излетели от складовете и домакинствата бръмбари или от късно засети
                    със заразени семена посеви.
                </p>
                <h4 class="my_class_h4">Стратегия за борба</h4>
                <p>
                    Прилагане на система от мероприятия, осигуряваща унищожаване на всички източници на зараза. Навременна сеитба
                    със здрави семена. Осигуряване на 4-5 km пространствена изолация на посевите от складовете и населените
                    места. Своевременно прибиране и овършаване на нахута, като семената се съхраняват в предварително добре
                    дезинфекцирани складове.
                </p>
                <p>
                    На полето борбата е насочена срещу възрастните бръмбари. Преди появата на първите зрели бобове се прави
                    отчитане за установяване плътността на неприятеля. Преглеждат се също и узрели бобове, като се търсят
                    повреди по семената. При наличие на бръмбари и нападнати семена се провежда инсектицидно третиране на посевите.
                </p>
                <p>
                    За унищожаване на скритата зараза в семената успешно може да се прилага термотерапията, т.е. използването
                    на ниски и високи температури. През зимните месеци семената да се съхраняват в проветриви складове или да
                    се оставят на тераси и под навеси, като се излагат на действието на ниски температури. В домакинствата
                    семената да се съхраняват в най-хладните помещения или на терасите, но изолирани от влага. Всички стадии
                    на неприятеля загиват при сума на ниските температури минус 110°С. При възможност семената могат да се
                    поставят в хладилник за пет денонощия.
                </p>
