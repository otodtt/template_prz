                <h3 class="my_class_h3">Чернилка – <span class="latin_name">Ascochyta rabiei</span></h3>
                <h4 class="my_class_h4">Описание на болестта</h4>
                <p>
                    По листата петната са кръгли тъмнокафяви до черни с по-тъмна периферия; по стъблата и разклоненията те са
                    тъмнокафяви до черни, продълговати, с пръстеновидна некроза, връхната част увяхва; по бобовете петната са
                    кръгли, кафяви с по-тъмна периферия; по семена петната са кафяви до кафяво-черни. Повредените тъкани пожълтяват,
                    листата окапват, бобовете се деформират, а семената губят кълняемостта си.
                </p>
                <p>
                    В петната се образуват плодни тела на гъбата (пикнидий). Патогенът се съхранява в семената. Съвършената
                    форма на гъбата <strong class="latin_name">Micosphaerella rabiei</strong> формира плодни тела (перитеции)
                    в болните растителни остатъци.
                </p>
                <p>
                    Източник на инфекция са болните семена и растителните остатъци. Разпространението на патогена през вегетацията
                    се осъществява със спори посредством дъждовните води.
                </p>
                <h4 class="my_class_h4">Стратегия за борба</h4>
                <p>
                    Използването на здрави семена, дълбокото заораване на растителните остатъци и правилното сеитбообращение
                    с пространствена изолация от миналогодишните ниви, устойчиви сортове (сорт Балкан) са мероприятията, включени
                    в Добрата растителнозащитна практика. Обеззаразяването на семената и третирането на посевите с фунгициди
                    ограничава развитието на болестта през вегетацията.
                </p>
