                <h4 class="my_class_h4">Описание на неприятеля</h4>
                <p>
                    <strong class="latin_name">Жълтата фасулева мозайка</strong> образува хлоротични, дори жълти листа, понякога
                    с некроза по нерватурата, водеща до окапването им. По-рядко се наблюдава потъмняване на кората на клонките.
                    При <strong class="latin_name">краставичномозаичната вироза</strong> - наблюдава се силно изоставане болните
                    растения и постепенното им изсъхване. По долната част на растенията се развива некроза, която преминава
                    нагоре по стъблото и листата, които пожълтяват и окапват. <strong class="latin_name">Люцернова мозайка</strong>
                    - хлороза или пожълтяване на листата, понякога с некроза по жилките и окапване. Рядко се наблюдава потъмняване
                    на кората на стъблото и клонките подобно на това при вируса на жълтата фасулева мозайка.
                </p>

                <h4 class="my_class_h4">Стратегия за борба</h4>
                <p>
                    Борбата с вирусните болести се извежда трудно, поради което загубите от тях са значителни. Тъй като няма
                    средства за лечение на вирусно болните растения, мерките за борба трябва да бъдат насочени към предпазване
                    от инфекция. Превантивните мерки за ефикасен контрол, които са в основата на Добрата растителнозащитна
                    практика, включват: използване на здрав посевен материал; унищожаване на източниците на инфекция
                    (болни растения и плевели), ограничаване разпространяването на вируса от векторите (листни въшки) чрез
                    третиране с инсектициди и минерални масла, както и избор на устойчиви и толерантни сортове.
                </p>
