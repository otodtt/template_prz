                <p >
                    Нахутът е старо културно растение. Отглежда се заради семената, които имат голямо съдържание на белтъчни
                    вещества, въглехидрати и известно количество мазнини, които го правят богата храна за човека и домашните
                    животни. По съдържание на белтъчини и въглехидрати той не отстъпва на фасула, лещата и граха, а по съдържание
