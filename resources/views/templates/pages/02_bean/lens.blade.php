                <h3 class="my_class_h3">Ръжда – <span class="latin_name">Uromyces fabae</span></h3>
                <h4 class="my_class_h4">Описание на болестта</h4>
                <p>
                    По листата напролет се появяват дребни групи от ецидии, а по-късно жълто-кафяви прашести уредосори, които
                    са закръглени до елиптични, със или без бледожълто-зелен венец. В края на вегетацията по всички зелени
                    части се образуват черни, прашести телейтосори. Нападението води до некроза около сорите, пригор и окапване
                    на листата, вследствие на което бобовете остават дребни, а семената недоразвити.
                </p>
                <p>
                    Гъбата е автоециден, облигатен патоген, чийто цикъл на развитие преминава изцяло върху лещата. Презимува
                    като телейтоспори, основно полепнали по семената. Болестта се развива при температура 20-22°С и висока влажност.
                </p>

                <h4 class="my_class_h4">Стратегия за борба</h4>
                <p>
                    Основните мероприятия, включени в Добрата растителнозащитна практика, са: спазване на сеитбообращение с
                    пространствена изолация от миналогодишни посеви; унищожаване на растителните остатъци; оптимална за условията
                    на региона сеитба; устойчиви сортове; третиране през вегетацията с подходящи фунгициди.
                </p>

                <h4 class="my_class_h4">Активни вещества</h4>
