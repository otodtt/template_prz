                </p>
                <p>
                    Гъбата се съхранява в заразените растителни остатъци и в почвата под формата на микросклероции. Разпространението
                    на болестта през вегетацията се осъществява с дъждовната вода, а нейното развитие се благоприятства от влажни
                    условия и температура в границите 20-24°С.
