        <div class="row" >
            <div class="pest-description-container col-md-10 text-justifysss text-indent">
                <h3 class="my_class_h3">Аскохитоза – <span class="latin_name">Ascochyta lentis</span></h3>
                <h4 class="my_class_h4">Описание на болестта</h4>
                <p>
                    По листчетата се формират малки, бързо нарастващи кафяви, кръгли петна с множество черни точици (плодните
                    тела на гъбата-пикнидии). Силно нападнатите листчета изсъхват и опадат; по стъблата и разклоненията петната
                    са кафяви, нарастващи пръстеновидно, в резултат на което органите над тях загиват; по бобове и семена петната
                    са кръгли, кафяви, покрити с пикнидии.
                </p>
                <p>
                    Гъбата се съхранява в заразените семена и растителните остатъци. Разпространението и през вегетацията се
                    осъществява с дъждовната вода. За развитието на болестта е необходима температура около 15°С и висока
                    относителна влажност.
                </p>

                <h4 class="my_class_h4">Стратегия за борба</h4>
                <p>
                    Използването на здрави семена, дълбокото заораване на растителните остатъци и правилно сеитбообращение с
                    пространствена изолация от миналогодишните ниви са основни мероприятия, включени в добрата растителнозащитна
                    практика. Обеззаразяване на семената и третиране на посевите с фунгициди ограничава развитието на болестта
                    през вегетацията.
                </p>
                <h4 class="my_class_h4">Активни вещества</h4>
                <p>Няма регистрирани.</p>
