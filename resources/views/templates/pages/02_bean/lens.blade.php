                <h4 class="my_class_h4">Стратегия за борба</h4>

                <p>
                    Добрата растителнозащитна практика включва комплексни мероприятия: агротехнически и химически за борба със
                    заплевеляването в посевите. Агротехниката, която се прилага при отглеждането на лещата, има значение за
                    унищожаването на плевелите. Основните агротехнически мероприятия в полета, засети с леща, са:
                    сеитбообращения - ротация с окопни и житни култури; подходящи срокове и начини (редово) на сеитба; гъстота
                    на посева; обработка на почвата (основната оран да се извършва рано, в съчетание с подходящи летно-есенни;
                    в периода след сеитба, преди поникване на лещата да се извършва „сляпо“ брануване, с него се унищожават
                    много от видовете, които изпреварват в развитието си културните растения; след поникване се препоръчват
                    още 1-2 бранувания до цъфтежа, когато плевелите са в най-ранни фенофази-1-2 листа).
                </p>
                <p>
                    Добър ефект в борбата срещу плевелите при лещата дава и използването на химически средства (хербициди).
                    Внасянето им трябва да стане след установяване на видовия състав и степента на заплевеляване на площите.
                </p>
                <p>
                    Използват се основно почвени хербициди, внесени след сеитба преди поникване срещу едногодишни, широколистни
                    плевели и при прогноза за смесено заплевеляване от едногодишни, житни и широколистни видове.
                </p>
                <p>
                    През вегетацията на лещата срещу житните видове могат да се прилагат листни хербициди.
                </p>

