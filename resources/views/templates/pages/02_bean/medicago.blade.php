            <div class="pest-description-container col-md-10 text-justifysss text-indent">
                <h3 class="my_class_h3">Черни петна – <span class="latin_name">Pseudopeziza medicaginis</span></h3>
                <h4 class="my_class_h4">Описание на болестта</h4>
                <p>
                    Напада всички надземни части - листа, стъбла и бобове. По петурите на листата се образуват точковидни, а
                    по-късно до 2-3 mm в диаметър тъмни петна с нарязани краища. В центъра от горната страна на петната се образуват
                    кафяви апотеции (0,3 до 1 mm), закрити в началото от епидермиса.
                </p>
                <p>
                    Причинителят <strong class="latin_name">Pseudopeziza medicaginis</strong> от клас
                    <span class="active-substance">Ascomycetes</span>. В апотеция се образуват бухалковидни асци с парафизи,
                    които съдържат едноклетъчни, елипсовидни аскоспори. Мицелът заразява директно през кутикулата и се развива
                    в епидермалните и паренхимни клетки, където формира строми. Гъбата зимува с апотеции в стромите на окапалите
                    листа. Болестта се явява масово при хладно и дъждовно време, гъсти посеви и кисели почви.
                </p>
                <h4 class="my_class_h4">Стратегия за борба</h4>
                <p>
                    Използване на устойчиви сортове люцерна (Обнова 10, Плевен 6, Виктория, Сторгозия, Вит, Вера). Създаване
                    на специализирани семепроизводни посеви - широкоредови или тесноредови, с намалена сеитбена норма, които
                    са по-проветриви и по-добре огрявани от слънцето. По-ранна коситба при масово развитие на болестта и третиране
                    след това с медни, цинебни, манебни, беномилови фунгициди.
                </p>
                <h4 class="my_class_h4">Активни вещества</h4>
                <p>Няма регистрирани продукти за растителна защита.</p>
