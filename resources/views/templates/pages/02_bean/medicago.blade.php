                    сортове, хербицидни третирания, третиране на семената с фунгицидни продукти за растителна защита по подходящи
                    методи, вегетационни третирания срещу неприятелите при достигане икономическите прагове на вредност.
                </p>
                <p >
                    В ДРЗП трябва да се използва подходяща техника, като се намали изтичането на продукти за растителна защита
                    и нежелателното им разсейване. За предотвратяване появата на резистентност да се избягва повторна употреба
                    на продукти за растителна защита с еднакъв механизъм на действие.
                </p>
                <p >
                    ДРЗП, препоръчвана от ЕРРО, изисква използването само на активни вещества, регистрирани в съответната страна
                    за даден вредител.
                </p>
                <p >
                    Люцерната се напада от голям брой гъбни, бактерийни, вирусни болести и от редица неприятели, но в зависимост
                    от почвено-климатичните и производствени условия не всички имат икономическо значение.
                </p>
