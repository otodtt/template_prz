            <div class="pest-description-container col-md-10 text-justifysss text-indent" >
                <h3 class="my_class_h3">Аскохитоза – <span class="latin_name">Ascochyta imperfecta</span></h3>
                <h4 class="my_class_h4">Описание на болестта</h4>
                <p>
                    По листата се образуват различни по форма, оцветяване и големина петна, кафяви до черни, често с жълт ореол.
                    По стъблата, разклоненията и бобовете петната са малки, тъмнокафяви, с масленозелен до жълт венец. Повреди
                    се наблюдават и по корена. Болните растения образуват дребни семена с кафяв цвят и набръчкана обвивка.
                    По всички повредени тъкани се наблюдават пикнидии.
                </p>
                <p>
                    Причинителят е <strong class="latin_name">Ascochyta imperfecta</strong> от клас
                    <span class="active-substance">Deuteromycetes</span>. Пикнидиите са черни и съдържат едноклетъчни и двуклетъчни,
                    цилиндрични спори, прави или леко извити. При влага те се извиват върху петната под форма на слизеста маса
                    с розов цвят. Гъбата се запазва със семето и остатъците като пикнидии и мицел. Болестта се явява масово
                    при хладно и влажно време.
                </p>
                <h4 class="my_class_h4">Стратегия за борба</h4>
                <p>
                    Използване на здрав или обеззаразен посевен материал; спазване на сеитбообръщение с житни, балансирано
                    минерално торене, създаване на просветлени семепроизводни посеви, по-ранна коситба и третиране с фунгициди
                    (<span class="active-substance bold">тирам, цинеб, манеб, беномил</span>).
                </p>
