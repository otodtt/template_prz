                    изисква основната обработка на почвата в зависимост от срока на сеитбата, да се извършва наесен или напролет с
                    лед прибиране на предкултурата. Тя включва плитка обработка за унищожаване на растителните остатъци,
                    подравняване и оран. Есенната оран се извършва на дълбочина 28-30 cm, а пролетната на 19-21 cm. ДРЗП налага
                    осигуряване на пространствена изолация на посевите най-малко от 0,5 до 1 km от едногодишните бобови култури,
                    използване на здрави, с добра кълняемост семена за посев, по-ранна сеитба, подходящ предшественик - най-подходящи
