                <p >
                    Люцерната е едно от най-старите културни фуражни растения, познати на човека. По значение като фуражно растение
                    тя стои на първо място между многогодишните бобови култури. Зелената маса или сеното на люцерната са извънредно
                    ценна храна за всички селскостопански животни. По съдържание на хранителни вещества и особено на белтъчини
                    тя заема едно от първите места между всички бобови и житни фуражни растения.
                </p>
                <p >
                    Семената поникват при температура 1°С. Младите току-що поникнали растения издържат на температура до -5°С,
                    а добре вкоренените многогодишни растения и държат до -20°С, а при дебела снежна покривка и до -40°С.
                    Люцерната издържа и на високи температури, но над 40°С спира образуването на нови пъпки. Благодарение на
                    дълбоката си коренова система тя може да се снабдява с вода и от по-долните пластове на почвата и не загива
                    в най-сушави години. Люцерната вирее почти във всички почви, стига да са достатъчно дълбоки и да съдържат
                    известни минимални количества вар. Преди сеитба площите да се наторят с фосфорни, калиеви и азотни торове.
                    Засяването на люцерната се извършва през пролетта или есента. Най-подходящо време за пролетна сеитба в
                    Северна и Югозападна България е началото до средата на март, а за южните райони - началото на март. Оптимално
