                <p>• По-ниска коситба и брануване на посевите през пролетта;</p>
                <p>• Изгаряне на растителните остатъци, които са източник на зараза;</p>
                <p>
                    • При масово нападение от неприятели и невъзможност за химична борба се препоръчва преждевременно окосяване.
                    След прибиране на сеното да се извърши третиране срещу падналите върху почвата възрастни и ларви;
                </p>
                <p>
