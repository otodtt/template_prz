                <h3 class="my_class_h3">Фузарийно увяхване - видове от род <span class="latin_name">Fusarim</span></h3>
                <h4 class="my_class_h4">Описание на болестта</h4>
                <p>
                    Причинява се от почвени гъби от същия род, които се развиват в проводящата система (трахеомикози). Болните
                    растения лесно се отскубват, корените са кафяви и изгнили. При старите растения листата им изсъхват и окапват,
                    при напречен разрез стъблата им са кафяво оцветени, изпълнени с мицелни влакна. Соевият храст постепенно
                    увяхва. Обикновено такива растения не формират бобове или те са недоразвити и неосеменени. Патогените се
                    запазват в болните растителни остатъци в почвата.
                </p>
                <h4 class="my_class_h4">Стратегия за борба</h4>
                <p>
                    Сеитбата да се извършва в оптимални за района срок и гъстота, на добре обработени почви. Унищожаване на
                    растителните остатъци и използване на здрави семена.
                </p>

