                    при температура -2-3°С. Нормалното развитие на културата изисква голямо количество вода през време на вегетацията.
                </p>
                <p >
                    Соята може да се отглежда почти на всички типове почви с изключение на тежките, заблатени и кисели почви.
                    За Северна България най-подходящото време за сеитба е първата половина на април, а за Южна-края на март-началото
