                <p >
                    Добрата растителнозащитна практика (ДРЗП) при соя (<span class="latin_name">Glycine hispida</span>) е част
                    от програмата на ЕРРО за изготвяне на подобни практики за основните култури, застъпени в ЕРРО - региона.
                    Тя ще бъде разработена в съответствие с ЕРРО-стандарт РР 2/1(1) за принципите на ДРЗП. Стратегията ще включва
                    методите за борба с основните вредители (вкл. патогени и плевели) по соя.
                </p>
                <p >
                    Соята принадлежи към най-старите културни растения, познати на човека. Тя се отглежда главно заради семената
                    които се отличават с богато съдържане на белтъчни вещества и мазнини. По съдържание на белтъчини в семената,
                    соята стои на едно от първите места между културните бобови и небобови растения. Зелената, надземна маса
                    съдържа голямо количество белтъчни вещества, което я прави добра храна за селскостопанските животни (за паша,
