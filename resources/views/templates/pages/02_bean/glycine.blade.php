                    - след сеитба-срещу едногодишни, широколистни видове
                    <span class="active-substance">линурон, прометрин, оксифлуорфен, метрибузин, метобромурон</span> (почвено);<br/>
                    - срещу едногодишни широколистни и преобладаващи едногодишни житни видове:
                    <span class="active-substance">ацетохлор, метолахлор, алахлор, пропизахлор, пендиметалин, диметанамид</span> (почвено)
