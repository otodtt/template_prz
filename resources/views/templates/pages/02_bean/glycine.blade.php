                <h3 class="my_class_h3">Плевели</h3>
                <p>
                    Соята е слаб конкурент на плевелите, особенно в началните фази от развитието си, което оказва отрицателно
                    влияние върху добива. Широко разпространени са едногодишните широколистни видове:
                    <span class="latin_name">черно куче грозде, свиница, татул</span>, а от многогодишните:
                    <span class="latin_name">балур, повитица, паламида</span>.
                </p>

                <h4 class="my_class_h4">Стратегия за борба</h4>

                <p>
                    Добра практика в борбата срещу плевелите при соята е прилагането на агротехнически и химически мероприятия.
                    От съществено значение е засяването на соята след житни със слята повърхност. Обработката на почвата да бъде
                    съобразена с почвения тип, характера и степента на заплевеляване. Навременното и качествено извършване на
                    основната оран има решаващо значение за ограничаване заплевеляването с многогодишни коренищни и кореновоиздънкови
                    плевели. Поникналите след това издънки могат да бъдат унищожени чрез подходящи хербициди. Допълнителните
                    есенни и пролетни предсеитбени обработки са ефективни за унищожаване на едногодишните плевели. Соята е
                    култура, която пониква бавно особено при студени и влажни условия и плевелите често поникват преди нея.
                    В този случай борбата срещу тях се извършва чрез брануване преди поникването на соевите растения. Прилага
                    се механичната борба чрез окопаване, като броят на обработките зависи от степента на заплевеляване и
                    уплътняването на почвата.
                </p>
                <p>
                    Химичните третирания са в зависимост от: наличието на плевелни семена в почвата преди поникване на соята,
                    видовия състав и степента на заплевеляване след поникване на културата, климатичните и почвените условия,
                    определящи ефективността на продукти за растителна защита. В края на вегетацията се извършва десикация на плевелите.
                </p>

