                    им са богата белтъчна храна за всички видове домашни животни.
                </p>
                <p >
                    Оптималната среднодневна температура за поникване на семената на соята е 18-20°С, а минималната-7-8°С.
                    За развитието на соята най-благоприятна се счита температура 18-22°С през вегетационния период. Соята не
