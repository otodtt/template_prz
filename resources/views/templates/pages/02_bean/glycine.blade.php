                <h3 class="my_class_h3">Мана – <span class="latin_name">Peronospora manjurica</span></h3>
                <h4 class="my_class_h4">Описание на болестта</h4>
                <p>
                    Болестта се проявява в две форми-системна и локална. При системната растенията остават по-ниски, с по-дребни
                    и по-малко на брой листа. Системно-хипертрофиращата форма засяга бутоните, цветовете и чушките. Те се деформират,
                    покафеняват и изсъхват. Семената се покриват масово с ооспори. При локалната форма по листата се образуват
                    с жълто-зелени закръглени петна, от долната страна са обхванати от сиво-бял до виолетов налеп. При масово
                    нападение листата изсъхват и окапват.
                </p>
                <p>
                    Честите превалявания разпространяват болестта, особено уязвими са сортовете соя с по-силно окосмени листа
                    и тънка кутикула. Благоприятни условия за развитие на маната са температури от 15 до 25°С, превалявания,
                    гъсти посеви, неспазване на сеитбообращения и засяване на чувствителни сортове.
                </p>
                <p>
                    <span class="latin_name">P. manjurica</span> развива междуклетъчен мицел, който се изхранва от растителните
                    клетки с помощта на хаустории. През устицата излизат конидионосци. Патогенът зимува като ооспори в растителните
                    остатъци и семената. Жизнеността им е до 2 г.
                </p>

                <h4 class="my_class_h4">Стратегия за борба</h4>
                <p>
                    Борбата с маната по соята е комплексна: сеитбообращения (от 3 до 5 г); отглеждане на устойчиви сортове;
                    използване на здрави или обеззаразени семена.
                </p>
                <h4 class="my_class_h4">Активни вещества</h4>
                <p>Няма регистрирани продукти за растителна защита.</p>
