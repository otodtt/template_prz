                <h3 class="my_class_h3">Рак по стъблата и пригор по бобовете и стъблата -
                    <span class="latin_name">Diaporte phaseolorum var. sojaecola; var. caulivora</span></h3>
                <h4 class="my_class_h4">Описание на болестта</h4>
                <p>
                    Семеделите се покриват с червеникаво-кафяви рани и язви; стъблата и листните дръжки са с червеникаво-кафяви
                    елипсовидни петна, които обхващат пръстеновидно стъблото и го пречупват. Болните растения са сухи и се
                    открояват от здравите и зелени соеви храсти. В края на вегетацията стъблата се покриват с точковидни, подредени
                    в ивици дребни, плодни тела, видими с просто око. Те са или разположени като тегелчета по дължината на стъблото,
                    или като облак на елипсоидни кръгове. Върху повърхността на бобовете се формират плодни тела. Семената са
