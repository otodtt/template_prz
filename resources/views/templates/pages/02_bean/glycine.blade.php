                <h4 class="my_class_h4">Описание на болестта</h4>
                <p>
                    Заболяването е широко разпространено и се причинява от почвени многоядни гъби от род <span class="latin_name">Fusarim.</span>
                    Семената не покълнват при силна зараза, често кълновете остават в почвата, силно са деформирани и се покриват
                    с различно обагрен от бяло до черевеникаво- виолетов мицел на патогена. Растенията, които се развиват от
                    такива кълнове, изостават в развитието си, закържавяват и загиват, без да дадат семена. Понякога по котиледоните
                    се формират кафяви петна, подобни на тези от бактерии, но при влага язвите се покриват с мицел и спороношение
                    на гъбата, което обхваща и растежния връх.
                </p>
                <p>
                    Болестта се развива във влажно време, при посеви, засети на места с некачествена обработка на почвата, при
                    твърде ранна сеитба, несъобразена с климатичните изисквания на сорта и района.
                </p>
                <h4 class="my_class_h4">Стратегия за борба</h4>
                <p>Сеитбата да се извършва в оптималния за района срок и на добре обработени почви.</p>

