            <p >
                Фасулът се използва като основна храна с висока биологична стойност, тъй като осигурява в лесно усвоима
                форма белтъци, въглехдрати, витамини, минерални соли и други физиологичноактивни вещества. Фасулът се отглежда
                за две основни цели:
            </p>
            <p >
                1. Фасул за зелено-за консумация, консервиране и замразен, фураж.
            </p>
            <p >
                2. 3рял фасул-за консумация и семенен материал, по-рядко за фураж.
            </p>
            <p >
                Кореновата система на фасула е слабо развита и разположена в повърхностния почвен слой. фасулът е топлолюбиво
                растение. Семената поникват при температура 10°С, но за дружно поникване се нуждаят от температура над 12°С.
                Оптимална температура за поникване е 18-22°С. Пониците и възрастните растения не понасят ниски
                температури и при -1°С загиват. Най-ранната сеитба у нас да се извърши около 15 април, след преминаването
                на последните пролетни слани. Последната възможна дата на сеитба е 15-20 юли. фасулът е взискателен към
                светлината и влагата, поради което се отглежда при поливни условия. Неравномерното поддържане на почвената
                влажност през вегетационния период води до намаляване на добива. Расте добре на различни почви, но е по-взискателен
                към тях от другите бобови култури. фасулът има лоша биологична самопоносимост и се развива добре след всички
                предшественици, като най-подходящи са зимните житни и цвеклото.
            </p>
            <p >
                Добрата растителнозащитна практика изисква основната обработка на почвата, в зависимост от срока на сеитбата,
                да се извършва наесен или напролет след прибиране на предкултурата. Тя включва плитка обработка за унищожаване
                на растителните остатъци, подравняване и оран. Есенната оран се извършва на дълбочина 28-30 см, а пролетната-на
                19-21 см. ДР3П налага осигуряване на пространствена изолация на посевите най-малко от 0,5 до 1 км. от
                едногодишните бобови култури, използване на здрави, с добра кълняемост семена за посев, по-ранна сеитба,
                подходящ предшественик, своевременно прибиране и овършаване на семената при зрелия фасул. Стратегиите за
                борба са в зависимост от наличните вредители в ценозата на фасула и включват различни комбинации с други
                култури в сеитбообръщения, използване на толерантни до устойчиви сортове, хербицидни третирания, третиране
                на семената с фунгицидни продукти за растителна защита по подходящи методи, вегетационни третирания срещу
                неприятелите при достигане икономическите прагове на вредност.
            </p>
            <p >
                ДР3П изисква използване на активни вещества, които са регистрирани в страната за съответните вредители по
                фасула. Те трябва да се прилагат с подходяща техника, като се намали изтичането на продукти за растителна
                защита и нежелателното им разсейване.
            </p>
            <p >
                Фасулът се напада от голям брой гъбни, бактерийни, вирусни болести и от редица неприятели, но в зависимост
                от почвено-климатичните и производствени условия не всички имат икономическо значение.
            </p>
