            <h3 class="my_class_h3">Антракноза – <span class="latin_name">Colletotrichum lindemuthianum</span></h3>
            <h4 class="my_class_h4">Описание на болестта</h4>
            <p>
                Антракнозата напада всички надземни органи на фасулевото растение. Петната по листната нерватура, листните
                дръжки, стъблото и разклоненията са елипсовидни, тъмнокафяви до черни с по-светъл център; по бобовете са
                кръгли до неправилни, тъмнокафяви до черни с по-светъл център; по семената тъмнокафяви до черни язвички.
                При влажни условия върху тях се отделя светлокафява течност, която при засъхване придобива розов оттенък.
            </p>
            <p>
                Източник на инфекция са семената (гъбата се съхранява под форма на мицел и спори в продължение на няколко
                години) и растителните остатъци.
            </p>
            <p>
                Разпространението на болестта през вегетацията се осъществява с дъждовните и поливни води. Заразяването и
                развитието на гъбата се благоприятства от висока влажност и температура в границите 18-22°С. Гъбата се
                характеризира със значително расово разнообразие. До този момент в страната са установени три раси.
            </p>

            <h4 class="my_class_h4">Стратегия за борба</h4>
            <p>
                Използването на здрав посевен материал е основното средство за борба с антракнозата по фасула. Останалите
                мерки в Добрата растителнозащитна практика включват: 2-3- годишна ротация с житни и други ненападащи се
                от гъбата култури, дълбоко заораване на растителните остатъци, унищожаване на първичната инфекция, използване
                на устойчиви сортове.
            </p>
