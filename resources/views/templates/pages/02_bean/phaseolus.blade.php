                от късни, пролетни плевели (обикновен щир, бяла куча лобода, черно куче грозде, пача трева, кръвно просо,
                кокоше просо, сива и зелена кощрява). От ранните пролетни плевели често се срещат полски синап и фасулче,
                а от многогодишните: повитица, паламида, балур, троскот и др.
