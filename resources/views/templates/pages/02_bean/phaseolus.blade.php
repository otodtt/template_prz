            <h3 class="my_class_h3">Ръжда – <span class="latin_name">Uromyces appendiculatus</span></h3>
            <h4 class="my_class_h4">Описание на болестта</h4>
            <p>
                От двете страни на листната петура се образуват лимоненожълти петънца, след няколко дни в центъра им, но
                само от долната страна се формират дребни, белезникави, чашевидни ецидии, които съдържат жълтеникава маса
                от ецидиоспори. По-късно се появяват кафяви, прашести купчинки (уредосори). При силно нападение листата
                прегарят и опадат. В края на вегетацията листата и стъблата се покриват с червено-кафяви телейтоспори.
            </p>
            <p>
                Ръждата по фасула има ограничено стопанско значение за България поради по-късната й проява. Болестта създава
                значителни проблеми при отглеждане на фасул като втора култура или при по-късна сеитба.
            </p>
            <p>
                Гъбата под форма на телейтоспори се съхранява в растителните остатъци. <span class="latin_name bold">U. appendiculatus</span>
                е автоециден, облигатен патоген, чийто цикъл на развитие преминава изцяло върху фасула. Разпространява се
                с въздушните течения и се благоприятства от хладни (16-25°С) и влажни условия през вегетацията. Гъбата се
                характеризира със значително патогенно разнообразие, като в страната са установени 7 раси.
            </p>
            <h4 class="my_class_h4">Стратегия за борба</h4>
            <p>
                Използването на устойчиви сортове, сеитбообращение с житни култури са основата на Добрата растителнозащитна
                практика за борба с ръждата. Устойчиви към разпространените в България раси са сортовете Абритус и Прелом.
            </p>

