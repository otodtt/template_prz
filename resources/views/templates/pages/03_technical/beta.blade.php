                <h4 class="my_class_h4">Описание на неприятеля</h4>
                <p>
                    Масово разпространение в цвекловите посеви у нас. Нападнатите от нематоди цвеклови растения имат бледозелени
                    листа, силно завяхващи през горещите часове на деня. Ларвите проникват през корените на растенията с
                    помощта на отделян от тях фермент. Образуват се гигантски клетки на растението, от които смучат сок.
                    В резултат на това старите корени отмират и растението се принуждава да коренообразува. Кореноплодът
                    се покрива от тъста „брада“ от коренчета, по които има бели тела на женските индивиди.
                </p>

                <h4 class="my_class_h4">Стратегия за борба</h4>
                <p>
                    Борбата е много трудна поради скрития й начин на живот. От значение е прилагане на сеитбообращение с
                    редуване на култури, които не са тостоприемници на този вид нематода.
                </p>
                <p>
                    Цвеклото трябва да се отглежда на едно и също поле най-малко след 4-5 години. Засяват се растения, които
                    провокират излюпването на яйцата в цистите, но не изхранват излюпените личинки. Най-подходящи за целта
                    са детелина, люцерна, грах за зърно, еспарзета, ръж, пшеница, ечемик, царевица и др. Значително
                    оздравяване на заразените полета може да се постигне и със систематични обработки на почвата и торене
                    с оборски тор за осигуряване на добра аерация на торния слой.
                </p>
