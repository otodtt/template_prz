                <p>
                    Многообразието от плевели и слабата конкурентоспособное на цвеклото срещу тях през цялата вегетация
                    затрудняват борбата с плевелите. Налага се да се прилага комплекс от мероприятия като подбор на чисто
                    от плевели поле от сеитбооборота, успешна борба с плевелите в предшестващата култура. Допустимо е
                    приложение на химически средства през лятно-есенния период на подготовка на площите за цвеклото с употреба
                    на хербициди на база глифозат срещу многогодишните плевели. От особено значение за ДРЗП в посевите от
                    цвекло са специфичните механизирани обработки, с които се достига унищожаване на плевелите до 60%.
                    Силно негативно действие върху цвеклото оказва <span class="latin_name">Sinapis arvensis</span>, който
                    пониква преди културата и бързо закрива посева. Подобно действие за цвеклото имат и други широколистни
