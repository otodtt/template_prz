        </div>
        <div class="row">
            <div class="pest-description-container col-md-10 text-justifysss text-indent" >
                <h3 class="my_class_h3">Чернилка – <span class="latin_name">Phytophthora parasitica var. nicotianae</span></h3>
                <h4 class="my_class_h4">Описание на болестта</h4>
                <p>
                    Чернилката е икономически важна болест за тютюна в България. Най-големи поражения тя нанася на тютюна,
                    отглеждан по долината на Струма в Петричко-Санданския район, по поречието на Места, по Рилското корито
                    и др. Патогенът напада тютюна във всички фази от развитието му. Болестта засяга предимно корените и
                    основите на стъблата. По младия разсад признаците трудно се различават от тези на сеченето. По основите
                    на растенията на напълно развития разсад гъбата понякога образува малки кафяви петна, без да причинява
                    загиването им. След разсаждането петната нарастват, причинявайки изсичане на стеблата близо до повърхността
                    на почвата, и растенията падат върху нея. Нападнатият на полето тютюн се отличава първоначално с
                    частично (в горещите часове на деня), а в последствие и с пълно увяхване. Основата на стъблата има
                    тъмно¬кафяв до черен цвят, а кората на потъмнялата част е хлътнала. При надлъжен разрез на стъблото се
                    вижда, че сърцевината е покафеняла, а по-късно при засъхване на стъблата се събира напречно на дискове,
                    между които има празни пространства. Понякога при по-влажно и топло време гъбата напада и листата, които
                    са в допир с почвата. По тях се образуват големи кафяви петна, състоящи се от няколко сферични пръстена
                    с по-светъл и по-тъмен цвят.
                </p>
                <h4 class="my_class_h4">Стратегия за борба</h4>
                <p>
                    В България има сортове тютюн, които притежават определена устойчивост към чернилка - Крумовград 90,
                    Рила 82, Рила 89, Мелник 812, Неврокоп 11-46. Те успешно се отглеждат в посочените рискови райони.
                </p>
                <p>
                    Агротехническите мерки ограничават в голяма степен развитието на болестта.
                </p>
                <p>
                    Разсадопроизводните участъци се разполагат на площи с лек механичен състав на почвата, без растителни
                    остатъци, защитени от вятър и осигурени с <span class="latin_name">постоянен чист водоизточник.</span>
                    Около лехите се поддържа фитосанитарна ивица чиста от плевелна растителност. Количеството и честотата
                    на поливките се определят от състоянието на разсада и влажността на почвата. От поникването на разсада
                    до приключване на разсаждането се извършват редовни прегледи за откриване поява на болестта.
                    Отглеждането на разсада се извършва на площи: чисти от токсични за тютюна пестицидни остатъци,
                    отдалечени от тютюневи сушилни и оранжерии, незаразени от галова нематода и синя китка.
                </p>
                <p>
                    Разсаждането се извършва равномерно и последователно, като най-напред се засаждат по-проветливите и
                    по-слънчеви места.
                </p>
                <p>
                    Торенето и подхранването на тютюна се извършва съобразно неговото развитие, запасеността на почвата с
                    хранителни вещества, здравното състояние на културата и др. Едностранчивото азотно торене благоприятства
                    развитието на болестта. Препоръчително е комбинираното торене с азот, фосфор и калий. С увеличаване
                    количеството на хранителните елементи калий и фосфор щетите от чернилка намаляват.
                </p>
                <p>
                    Важно значение за ограничаване на болестта са броят на поливките, количеството и качеството на поливната
                    вода. С намаляване на поливките се намаля и нападението от болестта. Полива се само при нужда и използва
                    вода от доказано чист източник, като не се допуска преминаването й през заразени площи.
                </p>
                <p>
                    В много случаи паразитът прониква в растенията през раните, причинени от насекоми, нематоди и при
                    обработката на почвата. Избягват се наранявания на растенията при разсаждането и окопаването им.
                </p>
                <p>
                    На разсадените тютюни се извършват редовни прегледи за здравословното им състояние. При констатиране
                    симптомите на заболяването се пристъпва към третиране с химически средства.
                </p>
                <p>
                    Непосредствено след приключване на беритбата тютюневите стебла се събират и унищожават, а площите се изорават.
                </p>
                <h4 class="my_class_h4">Активни вещества</h4>
                <p>
                    <span class="active-substance">Пропамокарб-хидрохлорид, алуминиев фозетил + фенамидон, металаксил + манкоцеб.</span>
                </p>
                <p>
                    Извършват се превантивни третирания, като тютюнът се напръсква 4-5 дни преди изнасяне разсада на полето
                    и 7-10 дни след разсаждане, когато се разсаждат чувствителни сортове тютюн на заразени площи.
                </p>
