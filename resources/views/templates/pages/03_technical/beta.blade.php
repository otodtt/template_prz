                    правилни сеитбообръщения с житни култури на тютюнопроизводните площи. Прилагат се редовни почвообработки
                    за унищожаване на плевелната растителност. Унищожават се чрез дълбоко изораване растителните остатъци
                    около лехите и тютюнарките след прибиране на тютюна. Голямо значение има борбата в разсада, защото
