        <div class="row">
            <div class="pest-description-container col-md-10 text-justifysss text-indent">
                <h3 class="my_class_h3">Фомоза (концентрични листни петна) – <span class="latin_name">Pleospora betae Berl.</span></h3>
                <h4 class="my_class_h4">Описание на болестта</h4>
                <p>
                    Фомозата е силно вредоносно заболяване по цвеклото, което вреди по всички органи на растението. Може
                    да причини гниене на кълновете, „сечене“ на пониците, кореново гниене и гниене на кореноплодите.
                </p>
                <p>
                    По листата се образуват закръглени едри светлокафяви концентрични зонирани петна. По тях се формират
                    пикнидиите на патогена като черни точици. По семенните растения стъбла и съплодия се развиват кафяви
                    петна с черни пикнидии. Запазва се със съплодията и маточните растения. Пораженията от патогена водят
                    до намаляване на продуктивността на семенниците и цвеклото и влошаване на качеството на продукцията.
                </p>
