            <div class="pest-description-container col-md-10 text-justifysss text-indent">
                <h3 class="my_class_h3">Церкоспороза (кръгли листни петна) – <span class="latin_name">Cercospora beticola Sacc.</span></h3>
                <h4 class="my_class_h4">Описание на болестта</h4>
                <p>
                    Причинителят на това заболяване ежегодно съпътства антотенетичното развитие на цвеклото. Гъбата зимува
                    в растителните остатъци след прибирането на културата под формата на строми. Започва развитието си при
                    t около 15<sup>o</sup>С. През вегетацията се разпространява с конидиоспори и при благоприятни условия,
                    т.е. t и влага, развива повече от 10 генерации. Икономическият праг на вредност е при поява на 1-3 петна
                    на 2-3 листа от растенията. Вредните последствия от пораженията от церкоспороза се състоят в намаляване
                    на добива с повече от 40%, снижаване на захарността с повече от два пункта, намаляване на масата от
                    листа и чела с повече от 60%, увеличаване на вредния азот, разтворимата пепел и натрия в кореноплодите.
                </p>
                <h4 class="my_class_h4">Стратегия за борба</h4>
                <p>
                    Най-важното звено от системата мероприятия против болестта е внедряване на устойчиви сортове. Задължителни
                    елементи са: въвеждане на сеитбообръщение (2-3 години); пълно минерално торене; борба с плевелите;
                    сеитба с обеззаразени семена.
                </p>

                <h4 class="my_class_h4">Активни вещества</h4>
                <p><span class="active-substance">флузилазол, меден оксихлорид пропиконазол, дифеноконизол, бромуконазол,
                        беномил, тиофанат-метил, флутриафол, меден хидроокис</span></p>
