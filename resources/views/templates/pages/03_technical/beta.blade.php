                <h3 class="my_class_h3">Брашнеста мана – <span class="latin_name">Euziphe communis f. betae Jas.</span></h3>
                <h4 class="my_class_h4">Описание на болестта</h4>
                <p>
                    Повсеместно разпространена, икономически важна болест по цвеклото. Проявява се като бял налеп от двете
                    страни на листната петура. Най-често листата се покриват изцяло с бял брашнест налеп. По-късно се
                    появяват „черни точки“ - плодните тела на гъбата. При семенниците болестта се развива по листата, цветните
                    листа, цветовете и съплодията.
                </p>
                <p>
                    Вредните последствия се изразяват в намаляване на добива от корени и влошаване на качеството на кореноплода.
                    При семенниците съплодията са недоразвити и окапват преждевременно.
                </p>
                <h4 class="my_class_h4">Стратегия за борба</h4>
                <p>
                    Спазването на пространствена изолация от семенници и технически посев и 4-5-годишен сеитбооборот намаляват
                    значително инфекцията на гъбата, която зимува като клейстотеции в растителните остатъци от цвеклото.
                </p>
                <p>
                    Поддържането на висока агротехника, минерално торене с по-високи дози N, използване на устойчиви сортове,
                    здрав посевен и посадъчен материал са важни елементи от технологията на цвеклото. Химичната борба
                    започва при достигане ПИВ, 10% нападната листна повърхност на растенията в посева.
                </p>
                <h4 class="my_class_h4">Активни вещества</h4>
