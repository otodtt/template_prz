                    нанасят големи щети на тютюнопроизводството. Те са едногодишни растения, които в основата си имат
                    луковично удебеление, здраво прикрепено за корените на тютюна, от които черпят вода и хранителни
                    вещества. Паразитът е в състояние напълно да унищожи тютюна. Главната разлика между двата вида е във
                    височината, оцветяването и разклонеността на стеблата. Паразитът се разпространява със семена чрез
                    вятъра, водите почвообработващите машини или тора на животни, хранили се с него.
                </p>
                <p>
                    По тютюна у нас от род <span class="latin_name">Cuscuta</span> са наблюдавани
                    <span class="latin_name">C. epithimum, C. europea, C. monogyna, C. pentagona</span>. Паразитът има тънко,
                    подобно на връв, силно разклонено стебло с жълтеникав до червено-кафяв цвят, с което обвива тютюна.
                    Свързва се с проводящите съдове на стъблата на тютюна посредством ризоподобни хаустории. Извличайки
                    вода, соли и органични вещества, паразитът препятства развитието на тютюна, като е в състояние да го
                    унищожи.
