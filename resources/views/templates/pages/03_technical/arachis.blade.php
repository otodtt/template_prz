                <h3 class="my_class_h3">Сиво гниене – <span class="latin_name">Sclerotinia arachidis</span></h3>
                <h4 class="my_class_h4">Описание на болестта</h4>
                <p>
                    Тази тъбна болест предизвиква гниене по всички части на растението, което външно се проявява с големи
                    жълтеникаво-кафяви петна по листата и потъмняване на стъблото. Листата и клонките над затнилата част
                    увяхват и изсъхват. При влага се покриват със сив спорообразуващ налеп. По плодовете и вътре в тях се
                    образуват черни склероции. Болестта се развива силно при чести летни валежи. Късните поливки и дъждовете
                    през есента допринасят за масовото им развитие.
                </p>
                <h4 class="my_class_h4">Стратегия за борба</h4>
                <p>
                    Спазване на правилно сеитбообращение, унищожаване на всички фъстъкови остатъци и да се избягват тежките,
                    влатозадържащи и непропускливи площи.
                </p>

                <h4 class="my_class_h4">Активни вещества</h4>
