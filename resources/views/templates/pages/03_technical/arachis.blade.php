                <h3 class="my_class_h3">Специфична сфера на действие</h3>
                <p>Този стандарт очертава принципите на Добрата растителнозащитна практика при фъстъци.</p>
                <h3 class="my_class_h3">Специфично одобрение и поправка</h3>
                <p >Първоначално одобрена през септември 2005 г. от НСРЗ</p>
                <br/>

                <p >
                    Фъстъците са една от най-разпространените маслодайни култури в света, но у нас се отглеждат предимно
                    за семената, които се консумират печени или като съставка на различни захарни изделия. Стъблата и
                    листата са ценен фураж. В сеитбообръщението фъстъците са отличен предшественик и обогатяват почвата
                    с азот. Те са топлолюбива култура и завършват вегетацията си при обща темпратурна сума около 3400 <sup>o</sup>С.
                    Отглеждат се предимно в районите на Пловдив и Благоевград където се засяват наши сортове с къс
                    вегетационен период. При наличие на влага в почвата фъстъците покълват при температура над 12<sup>o</sup>С
                    и никнат обикновено за 2-3 седмици. При ниски температури и изобилни валежи след сеитбата поникването
                    се забавя, не настъпва едновременно, а част от кълновете загиват. Затова семената преди сеитба се
                    третират с фунгицид (предимно с карбоксин+тирам). По своята природа фъстъците са светлолюбиви растения
                    и реагират силно на заплевеляването, особено в началните фази от развитието им. Те се изтеглят на
                    височина и образуват малко цветове и плодове. Нуждата на фъстъците от вода през отделните фази от
                    развитието им е различна. Най-много вода е необходима по време на цъфтежа и плодообразуването, които
                    протичат през юли и началото на август. По-късните поливки и дъждове удължават вегетацията и са причина
                    за нападение на листата и плодовете от причинители на сиво гниене, фузариоза, кафяви листни петна и др.
                    Фъстъците, засети на тежки черноземни почви, развиват по-голяма вегетативна маса, по-слабо плододаване
                    и са благоприятна среда за развитието на гъбни болести. Не трябва да се допуска отглеждането им на
                    почви, заплевелени с коренищни плевели - троскот, балур, паламида и др. Отглеждането на фъстъците в
                    сеитбообращение повишава добива от 20 до 60% и намалява риска от загуби от болести, насекомни и
                    ненасекомни вредители. В зависимост от запасеността на почвата е необходимо да се внасят различни
                    количества азотни, фосфорни и калиеви торове. Освен тези основни хранителни елементи фъстъците се
                    нуждаят от калций, магнезий, цинк, мед, молибден и др. Недостигът на някой от тези микроелементи
                    води до поява на хлорози. Особено силни те са на карбонатни почви, където добивът се намалява
                    силно - понякога десетократно.
                </p>
                <p >
                    Загубите от болести, неприятели, плевели и физиологични хлорози мотат да бъдат значителни. Налага се
                    внимателен мониторинг и комбиниране на различните методи за борба. ДРЗП препоръчва разумно използване
                    на химични средства, когато такива се налагат, и предлага начин за достигане на максимален ефект при
                    минимум въздействие върху полезни организми и околна среда. Спазването най-малко на двегодишно
                    сеитбообращение - в зърнопроизводителните райони след, житен предшественик, в памуковите - след памук,
                    в тютюневите - след тютюн, и др., намалява употребата на продукти за растителна защита. В борбата с
                    плевелите се използват агротехнически и химически методи.
                </p>
