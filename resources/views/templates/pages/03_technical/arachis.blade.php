                <h3 class="my_class_h3">Плевели</h3>
                <h4 class="my_class_h4">Описание на плевелите</h4>
                <p>
                    В условията на България фъстъците се отглеждат като късно пролетна култура при сравнително широки
                    междуредия и гравитачно напояване. При тези условия поникват и се развиват много плевели.
                    Конкурентоспособността на фъстъците е слаба. Семената на фъстъците поникват сравнително бавно - за 10-15
                    дни, а при ранна сеитба и хладна пролет за 20-30 дни. Тогава плевелите изпреварват поникването на
                    фъстъците и силно ти конкурират още в началните фази на развитие. В заплевелените посеви фъстъците силно
                    етиолират, цъфтят слабо и дават малко и лошокачествени плодове. Особено опасни са коренищните плевели
                    (балур, троскот и др.). При изваждането на фъстъците плевелите пречат на нормалната работа на нож-скобата,
                    получава се изрязване на част от плодовете.
                </p>
                <h4 class="my_class_h4">Стратегия за борба</h4>
                <p>
                    Най-ефикасно борбата с плевелите се извежда чрез добра система от културно-стопански и химични методи.
                    За да бъде предотвратен рискът от потенциална заплевеленост, предшествениците на фъстъците трябва да
                    оставят чисти площи. фъстъците се отглеждат най-често след житен предшественик. Лятната дълбока оран
                    трябва да се извърши непосредствено след прибирането на предшественика. При заплевеляване след
                    дълбоката оран се извършва култивиране или преораване на 15-18 см дълбочина. На места, където има
                    коренищни многогодишни плевели, веднага след дълбоката оран се извършва култивиране или брануване, за
                    да се изкарат коренищата на повърхността. Предсеитбената обработка на почвата започва още през март,
                    а последната непосредствено преди сеитбата. Механичната борба срещу пле¬велите може да продължи и
                    през вегетацията. Когато площите са по-големи и борбата с плевелите не може да се проведе с агротехнически
                    мероприятия, се използват хербициди.
                </p>

