        <div class="row" >
            <div class="pest-description-container col-md-10 text-justifysss text-indent">
                <h3 class="my_class_h3">Фузариоза по фъстъци – <span class="latin_name">род Fusarium</span></h3>
                <h4 class="my_class_h4">Описание на болестта</h4>
                <p>
                    Причинителите на болестта са гъби от <span class="active-substance">род Fusarium</span>. Те предизвикват
                    фузарийно увяхване, сухо кореново и стъблено гниене, сухо гниене на тенофори, плодове и семена.
                    Първите симптоми се наблюдават във фенофаза „масов цъфтеж“. Във фенофаза „плодообразуване“ болните
                    растения са най-много. Фузарийното увяхване се причинява предимно от вида
                    <span class="active-substance">Fusarium oxysporum</span>, който прониква през корените и се развива
                    в проводящата система и причинява типична трахиомикоза. Първоначално растенията завяхват в горещите
                    часове на деня, а по-късно то става трайно и необратимо. Други видове от
                    <span class="active-substance">род Fusarium</span> причиняват сухи локални некрози в основата на
                    стъблото, на корените и плодовете.
                </p>
                <h4 class="my_class_h4">Стратегия за борба</h4>
                <p>
                    Отглеждане на фъстъците в 3-4-тодишно сеитбообръщение.
                </p>

                <h4 class="my_class_h4">Активни вещества</h4>
                <p>За обеззаразяване на семена: <span class="active-substance">карбоксин + тирам</span></p>
