                <h3 class="my_class_h3">Кафяви листни петна – <span class="latin_name">Phylosticta arachidis</span></h3>
                <h4 class="my_class_h4">Описание на болестта</h4>
                <p>
                    Характерни признаци са многобройните закръглени кафяви петна по листата, ограничени от тъмен виолетово-кафяв
                    венец. По-късно в средната част на петното се появяват черни плодни телца. Паразитът зимува като пикнидии
                    в остатъците от заразените растения. Болестта се развива по-силно през дъждовни периоди.
                </p>
                <h4 class="my_class_h4">Стратегия за борба</h4>
                <p>
                    Събиране и унищожаване на остатъците от заразените растения. Спазване на сеитбообращение и пространствена
                    изолация от миналогодишни посеви. Прилагане на фунгициди само в случай на дъждовно лято.
                </p>
                <h4 class="my_class_h4">Активни вещества</h4>
                <p>Няма разрешени.</p>
