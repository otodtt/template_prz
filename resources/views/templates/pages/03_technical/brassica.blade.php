        <div class="row">
            <div class="pest-description-container col-md-10 text-justifysss text-indent" >
                <h3 class="my_class_h3">Сиво гниене – <span class="latin_name">Botrytis cinerea</span></h3>
                <h4 class="my_class_h4">Описание на болестта</h4>
                <p>
                    При влажни условия патогенът заразява рапицата, образувайки сиво-кафява маса от мицел с обилен брой
                    конидии по повърхността на заразеното стъбло, разклонения, листа и чушки. Съществува рязка граница между
                    здравата и болната тъкан при младите растения. По-късно тази разлика трудно се различава. По заразените
                    части се образуват склероции.
                </p>
                <p>
                    Заразените растителни остатъци в почвата са основен източник на зараза. Първите повреди мотат да се
                    наблюдават във фаза котиледон, но гъбата основно се разпространява, когато зимният период е отминал.
                    През пролетта повредите се появяват по презимувалите растения. По-късно заразяването зависи от комбинацията
                    на климатичните условия и повредите по растенията от насекоми. Най-често срещаните повреди са при
                    листната ос. Финалният цикъл на зараза може да се наблюдава по чушките. Ако растенията са инфектирани
                    рано (края на зимата), гъбата предизвиква гниене на листата и сърцевината на растенията. При късна
                    зараза инфекцията напредва от стъблата към чушките, предизвиквайки преждевременно зреене. Инфектираните
                    чушки дават нисък добив от семена. Заразата от <span class="latin_name">B.cinerea</span>
                    силно се влияе от климатичните условия.
                </p>
                <h4 class="my_class_h4">Стратегия за борба</h4>
                <p>
                    Превантивните мерки за ограничаване на болестта включват сеитба на оптимална гъстота - 30-40 растения
                    на м<sup>2</sup>. Избягване механичните повреди по растенията.
                </p>

