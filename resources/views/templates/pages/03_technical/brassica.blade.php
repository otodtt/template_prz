            <div class="pest-description-container col-md-10 text-justifysss text-indent">
                <h3 class="my_class_h3">Склеротинийно гниене – <span class="latin_name">Sclerotinia sclerotiorum</span></h3>
                <h4 class="my_class_h4">Описание на болестта</h4>
                <p>
                    Симптомите на това заболяване мотат да се открият в края на май - началото на юни. Около стъблото се
                    появява белезникав участък с дължина около 30 см. В този участък епидермисът лесно може да бъде обелен.
                    Инфектираните участъци са най-често в основата на листата. Тъмнокафяви склероции с големина 2 - 8 мм се
                    образуват в кухото стъбло. Болните растения изсъхват и стават чупливи. От преждевременно изсъхналите
                    и узрели растения се оронват семената.
                </p>
                <p>
                    Склероциите, които попадат в почвата по време на прибирането на реколтата или които остават в растителните
                    остатъци, са основният фактор за разпространението на болестта. Те остават жизнени в почвата от 7 до 12
                    години. Склероциите прорастват в апотеции през април - май, като изхвърлят аскоспори, с които заразяват
                    рапичните растения. Влажните условия стимулират формирането на апотеции и удължават срока на аскоспорното
                    заразяване. Заразяването се благоприятства ако съвпадне с пълния цъфтеж на растенията.
                </p>
                <h4 class="my_class_h4">Стратегия за борба</h4>
                <p>
                    Едно от основните изисквания е да се пазят почвите от нови заразявания със склероциите на гъбата.
                    Използване на чисти от склероции семена, 4-7-тодишно сеитбообращение и избягване на придшественици от
                    род Кръстоцветни и култури, които са чувствителни на
                    <span class="latin_name">Sclerotinia sclerotiorum</span> - картофи, тютюн, соя, фасул, слънчоглед и т.н.
                </p>

