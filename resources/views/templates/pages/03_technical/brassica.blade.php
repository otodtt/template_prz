                <h3 class="my_class_h3">Черни петна, тъмни петна по листата – <span class="latin_name">Alternaria brassicae</span></h3>
                <h4 class="my_class_h4">Описание на болестта</h4>
                <p>
                    При влажни условия патогенът заразява растенията през целия вегетационен период. Първите симптоми на
                    заболяването мотат да се наблюдават по котиледоните. По тях се появяват тъмнокафяви резки. Най-сериозни
                    обаче са повредите по чушките. По обвивката им се появяват черни петна, контрастно отделени от здравата
                    тъкан, покрити с конидии на гъбата. При нападение в млада възраст на чушките те се деформират. Заразените
                    чушки се свиват, разпукват преждевременно и семената падат върху почвата. Те са с ниско тегло и качество.
                </p>
                <p>
                    Заразените растителни остатъци (стъбла и листа) са основният източник на инфекция. Вятърът разнася
                    конидиите и заразява младите растенията. Редуването на влажни и сухи периоди благоприятстват развитието
                    на болестта, а най-подходяща температура за развитието на болестта е 18<sup>o</sup>С.
                </p>
                <h4 class="my_class_h4">Стратегия за борба</h4>
                <p>
                    Основно изискване е спазването на сеитбооборота и избягване сеитбата на рапицата на полета с наличие
                    на инокулум. Изораване на полетата възможно най-бързо след прибиране на реколтата.
                </p>
