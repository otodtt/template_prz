        <div class="row">
            <div class="pest-description-container col-md-10 text-justifysss text-indent">
                <h3 class="my_class_h3">Черно кореново гниене – <span class="latin_name">Thielaviopsis basicola</span></h3>
                <h4 class="my_class_h4">Описание на болестта</h4>
                <p>
                    Патогенът напада корените на тютюневите растения във всички фази от развитието им, но с най-голямо
                    значение е за разсадопроизводството. Първите признаци са изоставане в развитието и пожълтяване на
                    листата на растенията от по-старите нагоре. Кореновата система на такива растения е редуцирана, а
                    самите корени некротирали, кафяви или черни на цвят, докато тези на здравите са добре развити и с
                    чисто бял цвят. Загниването на кореновата система е сигурен признак за различаване на тази болест от
                    други заболявания. При осигуряване на определени условия болните растения могат да се съвземат, да
                    образуват нови коренчета и да преодолеят заболяването. С разсада болестта се пренася и на полето.
                    Ориенталските тютюни са по-устойчиви на това заболяване от едролистните. При тях пък сортовете Бърлей
                    са по чувствителни от сортовете Виржиния.
                </p>
                <h4 class="my_class_h4">Стратегия за борба</h4>
                <p>
                    Развитието на черното кореново гниене се благоприятства от ниски температури, висока почвена влажност
                    и тежки студени почви. За избягване появата или ограничаване развитието на болестта от първостепенно
                    значение е производството на здрав тютюнев разсад. За разсадопроизводство се избират леки, отцедливи
                    и топли почви с алкална реакция. Точно се спазва сеитбената норма за всеки тип тютюн. На заболелите
                    растения се преустановява подхранването с азотен тор и се презасипват със смес от равни количества
                    добре угнил оборски тор и пръст. За определен период се прекратяват или се разреждат поливките.
                </p>
                <p>
                    Известна е една раса на патогена, която напада тютюна. Някои от българските сортове тютюн притежават
                    устойчивост към това заболяване. Устойчивостта се изразява в бързо образуване на корк върху пукнатините
                    в корените образувани при разклоненията им поради бързото им развитие. Обикновено през тези пукнатини
                    патогенът атакува растенията.
                </p>
                <h4 class="my_class_h4">Активни вещества</h4>
