        </div>
        <div class="row">
            <div class="pest-description-container col-md-10 text-justifysss text-indent" >
                <h3 class="my_class_h3">Мана – <span class="latin_name">Peronospora tabacina</span></h3>
                <h4 class="my_class_h4">Описание на болестта</h4>
                <p>
                    Маната е една от най-важните икономически гъбни болести по тютюна. При поява на болестта в лехите се
                    наблюдават малки групи или петна от растения, които имат жълто-зелен цвят, а листата им се завиват
                    надолу. По долната страна на тези листа при влажно време се появява първоначално фин, белезникав налеп,
                    който по-късно се сгъстява и потъмнява до сив, дори син или синьо-виолетов. В полето признаците
                    най-напред се проявяват по най-долните листа на растенията, като бързо преминават към връхните.
                    Различават се две форми на проява на болестта - локална и системна. При локалната форма първоначално
                    се образуват слабо забележими хлоротични петна, които постепенно стават жълтеникави до интензивно
                    жълти с размери 2 - 2.5 см. При влажни ус¬ловия по долната страна на петната се появяват вече описания
                    налеп. При топло и сухо време картината е различна. Още преди появата на жълтеникавото оцветяване
                    петната се ограждат с некротични петънца, точици или чертички, поради което признаците могат да бъдат
                    сбъркани с някое вирусно заболяване. При благоприятни за развитие условия патогенът от листата може
                    да се разпространи по тъканите на растенията, минава в стеблото и достига връхните части. В този случай
                    растежът спира и растенията силно закържавяват - това е системната форма на болестта, която е фатална
                    за тютюна.
                </p>
                <h4 class="my_class_h4">Стратегия за борба</h4>
                <p>
                    Радикалният начин за решаване проблема с маната по тютюна са устойчивите сортове. От комерсиалните
                    сортове тютюн, отглеждани в България, с известна степен на устойчивост към мана са Крумовград 58,
                    Крумовград 90, Рила 82 и Рила 89, Неврокоп 11¬46. Едролистните тютюни особено сортовете от тип Бърлей
                    са по-чувствителни към болестта.
                </p>
                <p>
                    Появата и развитието на маната в нашата страна може се прогнозира със задоволителна точност,
                    благодарение на факта, че България, чрез Института по тютюна и тютюневите изделия (ИТТИ) в Пловдив
                    участва в международната система към КОРЕСТА (Център за координация на научните изследвания на тютюна)
                    за следене и сигнализиране появата, движението и развитието на маната в Средиземноморския район и
                    Европа, която функционира on-line в Internet.
                </p>
                <p>
                    Периодите на масово развитие на маната на полето могат да се прогнозират и по метода на
                    индексите (Moger 1964).
                </p>
                <p>
                    Правилните агротехнически мерки ограничават значително възможността за развитие на болестта. Подбират
                    се открити и проветливи площи за засаждане на тютюн. Осигурява се балансирано торене на растенията с
                    азот, фосфор и калий. Извършват се редовни прегледи на растенията в разсада и на полето за поява
                    симптоми на болестта. При поява на мана в лехите болните растения се унищожават, а на полето
                    засегнатите листа се обират и изнасят веднага.
                </p>
                <h4 class="my_class_h4">Активни вещества</h4>
                <p>
                    <span class="active-substance">Манкоцеб, метирам, цинеб, алуминиев фозетил, фенамидон, диметоморф +
                        манкоцеб, симоксанил + манкоцеб, металаксил + манбкоцеб</span>
                </p>
                <p>
                    След сигнал от Институт по тютюна и тютюневи изделия (ИТТИ) и Националната служба за растителна защита
                    (НСРЗ) за поява на болестта в някоя от съседните ни южни страни започват превантивни третирания на
                    тютюна с контактни фунгициди. При съобщение за поява на болестта в България се предприемат третирания
                    със системни или системно-контактни фунгициди.
                </p>
                <p>
                    Броят на третиранията се съобразява със степента на нападение на патогена и продължителността на
                    действие на използвания фунгицид.
                </p>
                <p>
                    Въпреки че вече има съобщения за резистентност на патогена към <span class="active-substance">металаксил</span>
                        в някои страни (Мексико, Австралия и др.) у нас такава не е наблюдавана.
                </p>
