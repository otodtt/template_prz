            <div class="pest-description-container col-md-10 text-justifysss text-indent">
                <h3 class="my_class_h3">Сечене на тютюневия разсад – <span class="latin_name">Pythium debaryanum, Rhizoctonia solani</span></h3>
                <h4 class="my_class_h4">Описание на болестта</h4>
                <p>
                    Болестта се проявява само по младите тютюневи растения в разсада. Критичният период за растенията
                    започва от момента на покълн ване на семената и продължава до образуване на дървесна тъкан. Част от
                    растенията загиват още преди да са достигнали повърхността на почвата. Тази фаза на болестта минава
                    незабелязано. Обикновената форма на сеченето се наблюдава по вече поникналите растения. Основите им
                    близо до повърхността на почвата добиват мътен, воднист вид и изглеждат като прищипнати, а често и
                    изсъхнали. Заболелите растения са свежи, със здрави корени, но полягат на групи върху почвата като
                    посечени. Не след дълго целите растения изгниват, сякаш се стопяват. От вече повалените растения
                    заразата се разпространява бързо върху съседните здрави растения, които загниват и изчезват по същия начин.
                </p>
                <h4 class="my_class_h4">Стратегия за борба</h4>
                <p>
                    Причинителите на сеченето обитават и се разпространяват във и чрез почвата, а развитието им се обуславя
                    от почвено-климатичните условия. Чрез правилни агротехнически мероприятия може да не се допусне или
                    чувствително да се ограничи развитието на болестта. За разсадопроизводство се подбират площи с лека,
                    отцедлива, лесно затопляща се почва, която не образува кора. Прилага се подходящата за всеки сортотип
                    тютюн сеитбена норма за осигуряване на добра гарнираност и аерация на растенията в лехите. Семената се
                    засяват на оптимална дълбочина 0.5 - 0.6 см. Задължително за мулчирането им се използва добре угнил и
                    чист от патогени, растителни и токсични остатъци оборски тор. Осигурява се диференциран поливен режим
                    на разсада в зависимост от фазата на развитие на растенията. Той се изразява в първоначално леки
                    (с малко вода), но чести поливки, а с развитието на растенията по-редки, но по-обилни поливки. Следи
                    се за правилния температурен режим, който да не е с големи денонощни амплитуди, и се осигурява добро
                    проветряване в полиетиленовите тунели.
                </p>
                <p>
                    При поява на болестта петната със заболели растения заедно с околните близки растения се унищожават
                    чрез поливане с 5 % разтвор на амониева селитра или 3 % разтвор от син камък.
                </p>
                <p>
                    Ако тези мерки не са достатъчно ефективни, се пристъпва към прилагане на фунгициди.
                </p>
                <h4 class="my_class_h4">Активни вещества</h4>
                <p>
                    <span class="active-substance">Пропамокарб-хидрохлорид, меден хидроокис, тритиконазол + ипродион</span>
                        (за обезза¬разяване на тютюневите семена)
                </p>
                <p>
                    Продукти за растителна защита се внасят с градинска лейка чрез поливане на лехите с разсад при разход
                    2 или 3 л/м<sup>2</sup> разтвор в зависимост от развитието на растенията. При необходимост третирането
                    се повтаря.
                </p>
