        <div class="row">
            <div class="pest-description-container col-md-10 text-justifysss text-indent">
                <h3 class="my_class_h3">Плевели</h3>
                <p>
                    В люцерновите посеви се срещат над двеста вида плевели от различни биологични групи. Поради бавния си темп
                    на растеж и развитие в първите два-три месеца след сеитбата люцерната е с изключително ниска конкурентна
                    способност към плевелите. Те са основен фактор за редуциране на биологичния потенциал на културата, създаване
                    на лошо гарнирани и краткотрайни посеви. В значителна част от плевелите се съдържат вредни алкалоиди и
                    етерични вещества, които влошават качеството на сеното и животинската продукция.
                </p>
                <p>
                    При висока степен на заплевеляване загубите на смилаем протеин от единица площ достигат до 47%. От чистите
                    семепроизводни посеви могат да се реколтират семена още в годината на създаване на посева, а не по
                    традиционния начин от втората година. При заплевеляване с някои икономически важни плевели (кускута,
                    канадска злолетница) може да се стигне до пълно компрометиране на люцерновото семепроизводство.
                </p>
                <h4 class="my_class_h4">Стратегия за борба</h4>
                <p>
                    Биологичните особености на люцерната (дребносеменна, многогодишна култура с бавен темп на растеж и развитие
                    след сеитбата) са определящи при направленията за извеждане на борбата с плевелите. В тази насока основните
                    направления са: предварителна борба с многогодишните плевели; борба с плевелите в годината на създаване на
                    посева; борба с плевелите при старите посеви.
                </p>
                <p><strong class="latin_name">Предварителна борба с многогодишните плевели:</strong></p>
                <p>
                    Борбата с многогодишните плевели (балур, пирей, троскот, поветица, паламида) трябва да започне основно
                    при предшественика и след неговото прибиране. Тя е най-ефективна, когато се прилага система от превантивни
                    мерки, съчетани с обработка на почвата и приложение на химическия метод. Класическите методи за обработка
                    на почвата чрез изтощаване и изсушаване на коренищните и кореновоиздънковите плевели, допринасят за
                    намаляване степента на заплевеляване. Тъй като многогодишните плевели се размножават и вегетативно, на тези
                    площи не трябва да се използват почвообработващи машини с режещи работни органи (фрези, дискови оръдия),
                    което спомага за бързото им разпространение. Най-висок ефект в борбата с плевелите от тази група се постига,
                    когато дълбоката оран се предшества от третиране на стърнището с тотални системни хербициди на база
                    <span class="active-substance">глифозат.</span>
                    Подходящият срок за третиране е начало на изкласяване на житните и начало на бутонизация на широколистните
                    плевели. Дълбоката оран се провежда около две-три седмици след третирането, а при необходимост се прави
                    и едно преораване на дълбочина около 15 cm.
                </p>

                <p><strong class="latin_name">Борба с плевелите в годината на създаване на посева:</strong></p>
                <p>
                    Качествено проведените предсеитбени обработки на почвата (култивиране и брануване) и валирането преди и
                    след сеитбата са благоприятна предпоставка за дружно поникване на семената, което повишава конкурентната
                    способност на люцерната. Висок ефект се постига при съчетаването на агротехническите мероприятия с хербициди,
                    които се прилагат преди сеитбата, след сеитбата, преди поникването или вегетационно.
                </p>
                <p>
                    Преди сеитбата на люцерната с инкорпориране на 8-10 см може да се третира с 720 g/l
                    <span class="active-substance">ЕПТС+антидот</span> срещу едногодишните житни (видовете кощрява, кокоше
                    просо) и широколистните плевели (обикновен щир, бяла куча лобода, звездица, стъблообхватна мъртва коприва,
                    тученица) и срещу поници от семена на многогодишни житни.
                </p>
                <p>
                    След сеитбата преди поникването може да се използва 960 g/l
                    <span class="active-substance">S-метолахлор</span> със същия спектър на действие.
                </p>
                <p>
                    Във фаза втори-четвърти същински лист на люцерната срещу едногодишни широколистни плевели са подходящи
                    600g/l <span class="active-substance">бентазон</span> и <span class="active-substance">пиридат</span>,
                    а срещу едногодишни житни плевели и балур от семе могат да се използват
                    125g/l <span class="active-substance">флуазифоп-П бутил</span>, 125g/l
                    <span class="active-substance">сетоксидим</span>, 100g/l <span class="active-substance">пропаквизафоп</span>,
                    100 g/l <span class="active-substance">бентазон</span> трябва да се
                    използва в система с някой от посочените противожитни хербициди. В противен случай при комбинираното им
                    внасяне се явява антагонизъм, при което се понижава ефектът от противожитния хербицид. С висока селективност
                    и широк спектър на действие е 100 g/l <span class="active-substance">имазетапир</span>, приложен в същите фази.
                </p>
                <p>
                    Срещу видовете лапад високоефективен е <span class="active-substance">асулам</span>,
                    приложен при височина на плевела 18-20 cm.
                </p>
                <p>
                    Добра алтернатива е отглеждането на люцерна за фураж в смесени посеви с ежова главица в съотношение
                    1,2:1,2 кг/дка или под покров овес - 1,2:8 кг/дка. При тези посеви житният компонент потиска развитието
                    на плевелите и се намалява необходимостта от третиране с продукти за растителна защита.
                </p>
                <p>
                    Когато са пропуснати възможностите за третиране с хербициди, може да се направи по-ранно покосяване преди
                    осеменяване на плевелите и при височина на люцерната не по-малко от 25-30 cm.
                </p>

                <p><strong class="latin_name">Борба с плевелите при старите посеви:</strong></p>
                <p>
                    През втората-третата година на отглеждане на люцерната започва естествено разреждане на люцерновия посев,
                    поради което празните места се заемат от плевели. Третирането с хербициди гарантира дълготрайността на
                    посева, добра гарнираност и висока продуктивност. За целта могат да се използват: 100 g/l
                    <span class="active-substance">имазетапир, метрибузин, диурон</span> или
                    <span class="active-substance">секбуметон</span>.
                    С изключение на <span class="active-substance">имазетапир</span>, който може да се използва и в началото на
                    вегетацията на люцерната, останалите хербициди се прилагат в невегетационния период, като по време на
                    третирането почвата не трябва да е дълбоко замръзнала и да няма снежна покривка.
                </p>
                <p>
                    При люцерновите семепроизводни посеви борбата с плевелите се извежда по същата схема, както при люцерната
                    за фураж. Особено внимание тук трябва да се обърне на паразитния плевел кускута и на канадската злолетница,
                    които са с икономически важно значение за люцерновото семепроизводство. Основен дял в борбата срещу
                    кускутата заемат предпазните и агротехническите мероприятия (чист посевен материал, сеитбообръщение, в
                    което се редуват култури, които не са гостоприемници-например житни, да не се допуска използване на недобре
                    угнил оборски тор и изхранването на животни с фураж, който съдържа жизнеспособни плевелни семена).
                    Значителен дял в борбата срещу кускутата заема химичният метод. Ако посевът е заразен на петна, то те
                    трябва да се окосят ниско (2-3 cm над почвената повърхност), да се изнесе зелената маса, а петната да се
                    третират не по-късно от три-четири дни след това с 10% разтвор на амониева селитра, 15% амониев сулфат,
                    0,5% разтвор на дикват или някой от по-долу посочените хербициди. В случай на масово заразяване на цялата
                    площ е необходимо възможно ниско окосяване на заразения подраст, изнасяне на окосената маса и не по-късно
                    след три-четири дни третиране с хербицидите 100 г/л
                    <span class="active-substance">имазетапир, глифозат</span> или <span class="active-substance">пропизамид</span>.
