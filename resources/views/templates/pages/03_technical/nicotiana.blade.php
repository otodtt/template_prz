                    Приложението на фунгициди срещу болестта задължително се извършва в комбинация с посочените по-горе
                    културални практики.
                </p>
                <p>
                    Продукти за растителна защита се внасят с градинска лейка чрез поливане на лехите с разсад при разход
                    2 или 3 л/м<sup>2</sup> разтвор в зависимост от развитието на растенията. По-ниската доза е за превантивно
                    третиране на лехите, а по-високата при поява на болестта. При необходимост третирането се повтаря.
                </p>
