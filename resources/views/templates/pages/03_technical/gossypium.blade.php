        <div class="row">
            <div class="pest-description-container col-md-10 text-justifysss text-indent" >
                <h3 class="my_class_h3">Трипси – <span class="latin_name">Thrips tabaci Lind; Thrips flavus Schane.</span></h3>
                <h4 class="my_class_h4">Описание на неприятеля</h4>
                <p>
                    Непосредствено след поникването на памука върху нето от плевелната растителност преминават трипсите -
                    тютюнев трипс (<span class="latin_name">Thrips tabaci</span> Lind.) и
                    <span class="latin_name">Thrips flavus</span> Schanc. Те се намножават при топло и сухо време, като
                    нападат най-младите и нежни листа и вегетационния връх на растенията. Повредените растения се разклоняват
                    вилообразно, а листата се деформират и накъсват. Трипсите се срещат през целия вегетационен период, но
                    пораженията им имат икономическо значение от поникване до бутонизацията на памука.
                </p>

                <h4 class="my_class_h4">Стратегия на борба</h4>
                <p>
                    Появата и опасността от намножаване на трипсите може да бъде прогнозирана чрез плевелната растителност
                    около блоковете.
                </p>
                <p>
                    Основно борбата се извежда чрез третиране с инсектициди, като при плътност 1 бр. на растение се извършва
                    едно третиране, а при наличие над 3 бр. - две третирания през 10-12 дни.
                </p>
                <h4 class="my_class_h4">Активни вещества</h4>
