                <h3 class="my_class_h3">Телени и лъжетелени червеи – <span class="latin_name">сем. Elateridae, сем. Tenebrionidae и сем. Alleculidae</span></h3>
                <h4 class="my_class_h4">Описание на неприятелите</h4>
                <p>
                    Неприятелите нанасят сериозни повреди от сеитбата на памука до 1-2 същински лист. Нагризват семената,
                    кълновете и правят ходове в надземната част на младите растения. Тези повреди са по-силно изразени при
                    ранна сеитба и хладна пролет, което води до удължаване на този период от вегетацията на памука. В
                    комбинация с кореновото гниене тези повреди водят до прореждането на посевите.
                </p>

                <h4 class="my_class_h4">Стратегия за борба</h4>
                <p>
                    Необходимо е да се установи плътността на почвообитаващите неприятели още през есента, като тези проучвания
                    продължават и през пролетта, преди сеитбата на културата. Основната стратегия цели установяване на
                    потенциалната и реална опасност от тези неприятели за културата. При наличие на висока плътност е необходимо
                    да се проведе двуслойна оран (10-15; 15-30 cm) и се третират предсеитбено семената.
                </p>
                <h4 class="my_class_h4">Активни вещества</h4>
                <p>Обеззаразяване на семената: <span class="active-substance">тиодикарб.</span>
                </p>
