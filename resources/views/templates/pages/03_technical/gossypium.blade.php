                <h4 class="my_class_h4">Описание на неприятеля</h4>
                <p>
                    Най-осезателни поражения по памука нанася памуковата листна въшка
                    <span class="latin_name">Aphis gosswpii</span> Glov. Нейното намножаване по памука започва непосредствено
