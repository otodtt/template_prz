                </p>
                <p >
                    Шротът, получен от преработката на семето, е богат на белтъчини - 40 - 43 %, от които 33 % са смилаеми.
                    Обвивките на семената се използват за торене, за производство на амбалажна хартия, изолационни материали
                    и др. Късите влакна (линтът), получени след делинтирането на семената, се употребяват за получаване на
