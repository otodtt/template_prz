            <div class="pest-description-container col-md-10 text-justifysss text-indent">
                <h3 class="my_class_h3">Кореново гниене – <span class="latin_name">Fusarium sp.; Rhizoctonia sp.; Pythium sp.)</span></h3>
                <h4 class="my_class_h4">Описание на болестта</h4>
                <p>
                    Болестта е разпространена повсеместно. Причинява се от почвообитаващите микроорганизми от родовете
                    <span class="latin_name">Fusarium, Pythium u Rhizoctonia</span>. Те предизвикват загниване на семената,
                    кълновете, основата на стъблото. Има икономическо значение до появата на 3-4 същински лист. Развитието
                    на болестта се благоприятства от хладна и дъждовна пролет, когато се забавя поникването на памука, а
                    младите растения са с понижена жизненост.
                </p>
                <h4 class="my_class_h4">Стратегия за борба</h4>
                <p>
                    Борбата с болестта е насочена към създаване на благоприятни условия за растеж и развитие на младите
                    памукови растения. Спазване на сеитбообращения, качествена предсеитбена обработка, оптимални срокове
                    на сеитба, оптимална дълбочина на сеитба - 4-5 cm, своевременно разрохкване на почвената кора, подхранване
                    с азотни торове и др. За директна борба с кореновото гниене се препоръчва дилентиране на семената със
                    сярна киселина и обеззаразяването им.
                </p>

                <h4 class="my_class_h4">Активни вещества</h4>
                <p><span class="active-substance">Каптан, манкоцеб, металоксил-М, тритиконазол + ипродион.</span></p>
