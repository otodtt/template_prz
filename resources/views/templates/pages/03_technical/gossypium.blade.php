                <h3 class="my_class_h3">Плевели</h3>
                <h4 class="my_class_h4">Описание на плевелите</h4>
                <p>
                    Памукът е особено чувствителен към заплевеляването в началния етап от развитието си. Критичен период
                    е междуфазният период - втори същински лист - бутонизация. Плевелите потискат растежа на културата,
                    забавят развитието и, увеличават процента на окапване на плодните кутийки, благоприятстват разпространението
                    на трипса и листните въшки, силно влошават условията за механизираното му прибиране, а също и добива.
                    Най-разпространените плевели в посевите на памука са късните пролетни видове:
                    <span class="latin_name">Amaranthus retroflexus, Chenopodium album, Solanum nigrum, Hibiscus trionum,
                        Setaria spp., Echinochloa crus-galli</span>. През последните години нарасна заплевеляването с
                    <span class="latin_name">Abutilon theophrasti, Xanthium strumarium, Datura stramonium</span>.
                    От многогодишните плевели най-разпространен е упоритият кореново-издънков вид
                    <span class="latin_name">Convolvulus arvensis</span>. Съотношението едногодишни, многогодишни плевели
                    в памуковите посеви на страната е 1,6:1.
                </p>
                <h4 class="my_class_h4">Стратегия за борба</h4>
                <p>
                    Според ДРЗП борбата с плевелите в памуковите посеви трябва да бъде научно - обоснована и да се води
                    чрез правилно съчетаване на агротехническите мероприятия с химичната борба. От агротехническите мероприятия
                    особено значение има правилната обработка на почвата, извършена в оптимални срокове. След ранен
                    предшественик (пшеница, ечемик) обикновено се извършват подметка на стърнището, дълбока оран и 1-2
                    допълнителни обработки през есента.
                </p>
                <p>
                    Чрез пролетните предсеитбени обработки се подобрява структурата на почвата и се унищожават поникналите
                    плевели. През вегетацията на памука се извършват 2 механизирани и 1-2 ръчни окопавания. Първото
                    окопаване трябва да се извършва след изчерпване на хербицидния ефект, а следващите - в зависимост от
                    заплевеляването на почвата и образуването на почвена кора.
                </p>
                <p>
                    Други важни агротехнически мероприятия са: изборът на чисти от плевели площи, подходящи сеитбообръщения
                    с житни култури, диференцирано торене и т. н. Изборът на хербициди трябва да е диференциран, в зависимост
                    от заплевеляването. За тази цел трябва да се извърши предварително картиране на площите.
                </p>

                <h4 class="my_class_h4">Активни вещества.</h4>
                <p>
                    На площи, заплевелени предимно с едногодишни житни плевели и някои широколистни:
                </p>
                <p>
                    - преди сеитба с инкорпориране <span class="active-substance">трифлуралин</span>.
                </p>
                <p>
                    - след сеитба преди поникване <span class="active-substance"> - алахлор, ацетохлор, пендиметалин,
                        S-метолахлор, тиобенкарб, пропизохлор</span>.
                </p>
                <p>
                    През вегетацията на памука, във фаза 3-5-и лист на едногодишните житни или 15-20 cm височина на
                    многогодишните житни плевели:
                    <span class="active-substance">квизалофоп-П-етил, флуазифоп-П-бутил, халоксифоп-R-метил, пропаквизафоп + сърфактант, сетоксидим + масло + сърфактант.</span>.
                </p>
                <p>
                    На площи, заплевелени с едногодишни двусемеделни плевели -
                    <span class="active-substance">прометрин, оксифлуорфен, флуометурон, цианазин</span>.
                </p>

                <p>
                    При смесено заплевеляване с едногодишни житни и широколистни -
                    <span class="active-substance">изоксафлутол, оксидиаржил</span>.
                </p>
                <p>
                    Срещу едногодишни и многогодишни едносемеделни и двусемеделни плевели преди сеитба на културата, при
                    поникнали плевели -
                    <span class="active-substance">глифозат.</span>.
                </p>

                <h4 class="my_class_h4">Дефолианти</h4>
                <p>Главното предназначение на химичната дефолиация е чрез обезлистване на памуковите растения да се засили
                    процесът на узряването, като кутийките се развиват по-бързо, а също да се създаде възможност за
                    механизирано прибиране на суровия памук.</p>
                <h4 class="my_class_h4">Активни вещества.</h4>
                <p>
                    Като дефолианти при памука се използват:
                    <span class="active-substance">дикват, диметипин, етофен + цикланилид, глюфозинат.</span>.
                </p>
