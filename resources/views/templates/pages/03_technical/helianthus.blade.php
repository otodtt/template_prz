                <h3 class="my_class_h3">Мана по слънчогледа – <span class="latin_name">Plasmopara halstedii</span></h3>
                <h4 class="my_class_h4">Описание на болестта</h4>
                <p>
                    <span class="latin_name">Plasmopara halstedii</span> = <span class="latin_name">Plasmopara helianthi</span>
                    е облитатен паразит, който зимува в болните растителни остатъци под формата на ооспори. След изтниване
                    на заразените участъци ооспорите на гъбата се освобождават. През пролетта образуват зооспори. Част от
                    тях се отнасят от ветровете и заразяват нови растения и площи. Останалите проникват през кореновите
                    власинки и епидермиса на младите корени. Гъбата поразява всички органи на слънчогледовото растение.
                    Мицелът прониква постепенно от долу на горе във всички тъкани на гостоприемника. Това заразяване се
                    нарича първично за разлика от вторичното, което става предимно с конидиоспори, които, носени от вятъра,
                    попадат по листата. Вторичното заразяване е без икономическо значение. Болните растения изостават в
                    растежа, остават със скъсени междувъзлия и развиват съвсем малки, обърнати нагоре пити. По долната
                    страна на листата се появява бял налеп - конидионосците и конидиите на гъбата. Конидионосците лесно се
                    разнасят от вятъра и осъществяват вторичното заразяване.
                </p>
                <h4 class="my_class_h4">Стратегия за борба</h4>
                <p>
                    Най-добрият начин за ограничаване на заболяването е да се използват устойчиви сортове и хибриди слънчоглед.
                    Болестта може да се ограничи и с агротехнически средства като унищожаване на самосевки от слънчоглед,
                    сеитба в оптимални срокове - при достигане на почвена температура, благоприятстваща бързото развитие
                    на младите растения.Основен начин за борба с болестта е предсеитбеното третиране на семената.
                </p>

