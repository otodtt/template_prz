        <div class="row">
            <div class="pest-description-container col-md-10 text-justifysss text-indent">
                <h3 class="my_class_h3">Сиви петна по слънчогледа, фомопсис – <span class="latin_name">Diaporthe helianthi</span></h3>
                <h4 class="my_class_h4">Описание на болестта</h4>
                <p>
                    <span class="latin_name">Diaporthe helianthi Munt-Cvet et al.</span>, безполова форма
                    <span class="latin_name">Phomopsis helianthi Munt-Cvet et al</span>. е гъбно заболявания по слънчогледа.
                    Първите признаци на заболяването се появяват в края на юли - началото на август, съвпадащо с фаза начало
                    на цъфтеж. Причинителят на заболяването поразява листата, листните дръжки, стъблата, а в условията на
                    нашата страна - много рядко питите на слънчогледа. Първите петна се появяват по листните петури по
                    средните етажи на растенията. Инфекцията започва от върха или странично на листната петура. Листата бързо
                    увяхват и отмират, като остават да висят на стъблото. От листа инфекцията протича по листната дръжка
                    и достига до стъблото. Там се образуват елипсовидни петна, които понякога обхващат от едно до няколко
                    междувъзлия. Петната са светлокафяви в началото на заразяването, а в края на вегетационния период силно
                    просветляват и променят цвета си до сив. Некрозата протича и в дълбочина, като засята тъканите до
                    сърцевината. Тя се разрушава напълно, което прави стъблата лесно чупливи. Причинителят на болестта зимува
