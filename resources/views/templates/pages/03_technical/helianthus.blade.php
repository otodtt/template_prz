        <div class="row">
            <div class="col-md-12 text-justifysss text-indent">
                <h3 class="my_class_h3">Специфична сфера на действие</h3>
                <p>Този стандарт очертава принципите на Добрата растителнозащитна практика при слънчотлед.</p>
                <h3 class="my_class_h3">Специфично одобрение и поправка</h3>
                <p >Първоначално одобрена през септември 2005 г. от НСРЗ</p>
                <br/>

                <p >
                    Бързото нарастване на площите и производството на слънчоглед е продиктувано от увеличената консумация
                    на растителни масла в почти всички райони на света за сметка на мазнините от животински произход, от
                    високата хранителна стойност и оптималното съчетание на масло-киселинния състав на маслото.
                </p>
                <p >
                    Големият интерес на производителите на слънчогледово семе и ускореното разширяване на площите се обуславят
                    от икономическата изгода и високата рентабилност на културата. При сравнително високи и стабилни добиви
                    от единица площ между полските маслодайни култури производството на слънчогледово семе се поддава на
                    пълна механизация с минимални трудови разходи по отношение на културата. Освен за получаване на масло
                    слънчогледовите семена са източник и за добиване на кюспе и шрот, ценни високо-белтъчни фуражи за пряко
                    изхранване на животните или за приготвяне на пъл¬ноценни комбинирани фуражни смески. Семената съдържат
                    20-40% протеини, 30-60% масло, в т.ч. 80% линолова киселина. Слънчогледът представлява и полезен източник
                    на нектар за пчелите. Основно в нашата страна се отглеждат хибриди слънчоглед. Те са заместили свободно
                    опрашващите се сортове поради възможността за увеличаване на добива, устойчивостта спрямо вредители
                    (ръжда, мана), еднородността (улесняваща прибирането на реколтата) и самосъвместимостта (намаляваща
                    необходимостта от пчели и други насекоми опрашители в процеса на кръстосаното опрашване). Новосъздадените
                    хибриди слънчоглед и прилагането на добрата растителнозащитна практика са важен фактор за печелившо
                    производство на слънчоглед. Повсеместна практика в стопанствата е третирането на семената с фунгициди
                    и инсектициди с цел опазване на младите поници.
                </p>
                <p >
                    Слънчогледът лесно се приспособява към отделните почвени типове и разновидности. Съществува схващане,
                    че той може да се отглежда на всички почви, на които е възможно реколтирането на есенните житни култури -
                    пшеница и ечемик. Реакцията на растенията към механичния състав на почвата е относително еднаква както
                    при условията на леките песъчливи, така и при тежките глинести почви. За леките песъчливи почви
                    формирането на добива се определя от нивото на торене, а за тежките глинести почви - от излишъка на
                    влага в почвата през някои фази от развитието на слънчогледа. Високото съдържание води до прекомерен
                    растеж на растенията, забавяне узряването на слънчогледовите пити и увеличаване чувствителността им
                    към гъбните патотени. Препоръчват се съответни нива на фосфора и калия. Растенията се развиват най-добре
                    при неутрална и слабо кисела реакция на почвата. Почвеното легло на семената трябва да бъде влажно,
                    твърдо и свободно от плевели. Сеитбата трябва да бъде извършена непосредствено след последната обработка
                    на почвата.
                </p>
                <p >
                    Слънчогледът е високо рискова култура по отношение на загуби от болести, насекоми, птици и плевели.
                    Това налага спазване на указанията в ДРЗП, базирани на използване праговете на вредност (ако има такива),
                    системи за сигнализиране (ако има такива), внимателен мониторинг и комбиниране на различни методи за
                    борба с цел поддържане популациите на вредителите под нивата, причиняващи неприемливи щети по отношение
                    качеството на реколтата. ДРЗП препоръчва разумно прилагане на химични методи, когато такива се налагат,
                    и предлага начини за достигане на максимална ефективност и свеждане до минимум въздействието върху
                    организми, които не са обект на контрол (полезни видове), и околната среда. Вредителите по слънчогледа
                    не са разпространени равномерно в посева, поради което посевите трябва да бъдат проверявани на няколко
                    места. Обикновено един преглед седмично е достатъчен, но броят на проверките трябва да бъде увеличен,
                    когато числеността на вредителите бързо нараства или се доближи до прага на икономическа вредност.
                    Препоръчва се 5-6-тодишно сеитбообращение (преди на дадена площ отново да бъде засят слънчоглед). Това
                    ще спомогне за намаляване популациите на редица икономически важни специализирани болести по слънчогледа.
                    Някои патотени нападат и други култури (синап, соя, фасул), които поради тази причина не трябва да бъдат
                    включвани в сеитбообращенията на слънчогледа. В борбата срещу плевелите се използват основно два метода:
                    културно-стопански и химичен.
                </p>
                <p >
                    Слънчогледът трябва да се сее непосредствено след последната обработка на почвата, за да може да се
                    постигне бързо поникване на семената и да са по-конкурентноспособни в сравнение с плевелите. Плевелите
                    най-често поникват преди културата, особено в условията на хладно време, и борбата срещу тях може да
                    бъде изведена чрез брануване, преди поникване на слънчогледа. Механичната борба срещу плевелите след
                    поникване на културата се извършва чрез брануване и култивиране.
                </p>
                <p>
                    За борба срещу някои плевели се използват химични продукти за растителна защита за предсеитбено третиране
                    на семената или за третиране след сеитбата на слънчогледа, но преди поникване на културата.
                    Слънчогледът е чувствителен спрямо много от хербицидите, внасяни след поникване и използвани основно
                    в съседни посеви (напр. глифозат, дикамба, 2,4 Д и др.).
                </p>
